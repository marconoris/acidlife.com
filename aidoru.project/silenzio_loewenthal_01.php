  line: <b><h4>Apologia del silenzio</b></h4></p>
<p>Certe volte mi 
  viene di pensare che un poco di silenzio farebbe bene a tutti, a chi parla e 
  a chi ascolta. O meglio, a chi prende la parola nel senso letterale dell’espressione: 
  se ne appropria, per deturparla o anche solo per farle dire scempiaggini. Qualche 
  giorno fa sulla rubrica di lettere della Stampa compariva lo stizzito messaggio 
  di un ventenne che invitava il giornale a distinguere tra i naziskin, sporchi 
  brutti e cattivi, e i veri seguaci del nazionalsocialismo, eroici "lupi solitari" 
  amanti dell’ordine, della pulizia e della disciplina. Insomma, questo giovin 
  signore non avrebbe fatto un servizio migliore a se stesso e agli altri, tacendo? 
  Ma è vero che spesso serbare il silenzio è più impegnativo che parlare a briglia 
  sciolta. Anche perché, credo, il silenzio non è propriamente il contrario di 
  rumore o meglio, dell’assenza di comunicazione. Anzi. Per questo, come dicevo 
  qualche tempo fa, l’ebraico conosce molti modi per dire "silenzio", e l’italiano 
  risponde con fatica. V’è in ebraico il silenzio che si potrebbe raffigurare 
  come quello proveniente dai rumori prodotti in una stanza sigillata, donde nulla 
  riesce a uscire. Vi è il silenzio che è semplicemente "quiete", riposo dei sensi. 
  Vi è il silenzio che è mettere a tacere, un imperativo. Vi è il silenzio prodotto 
  dalla sordità, per il quale l’ebraico usa una radice che significa anche "solco", 
  "scavare" e che, con un lievissimo anagramma diventa "fruscio". Il silenzio 
  più bello è quello che sente Elia sul monte e che racconta la rivelazione divina, 
  un silenzio contraddittorio dal suono gentile: qol demamah daqah, cioè, più 
  o meno "voce silenzio sottile". Dio non era nel tuono né nel sussulto della 
  terra, non era nel fuoco né nella tempesta, piuttosto in questo flebile non-suono 
  che scavalca i sensi e arriva dritto al cuore. Una volta ho provato a comporre 
  una specie di "alfabeto del silenzio", per dare un senso anche in italiano alla 
  polifonia e polisemia del silenzio ebraico. Lo lascio qui ai miei lettori, per 
  chi ha voglia di seguirmi in questo cammino.</p>
<p>(21 agosto 2000) 
  elena.loewenthal@lastampa.it</p>
