<div class="ver2"><p><b><h4>Un uomo il 
  suo fiume e il ponte di Paulo Cielho</b></h4></p>
<p>Un uomo, dopo 
  molti anni di lavoro e di meditazioni sul miglior modo per attraversare il fiume 
  davanti alla sua casa, costruì una passerella. Si racconta però che gli abitanti 
  del villaggio raramente osavano passarvi sopra, a causa della sua precarietà. 
  Un bel giorno, da quelle parti comparve un ingegnere che, con l’aiuto della 
  gente del posto, costruì un ponte, la qual cosa mandò su tutte le furie il costruttore 
  della passerella. Questi, infatti, da quel momento incominciò a dire a quanti 
  avevano la pazienza di ascoltarlo che l'ingegnere aveva mancato di rispetto 
  nei confronti del suo lavoro. «Ma la passerella è ancora lì - rispondevano gli 
  abitanti del villaggio - ed è un monumento ai suoi anni di fatica e di meditazione». 
  «Nessuno però la usa», ribatteva l'uomo, stizzito. «Lei, signore, è un cittadino 
  rispettabile e noi siamo fieri di lei. Tuttavia, se la gente trova il ponte 
  più bello e utile della sua passerella, che cosa ci possiamo fare?». «Il ponte 
  attraversa il mio fiume!». «Ma signore, con tutto il rispetto che abbiamo per 
  il suo lavoro, vorremmo dirle che il fiume non le appartiene. Può essere attraversato 
  a piedi, in barca, a nuoto, o in qualsiasi altro modo: se le persone preferiscono 
  attraversarlo utilizzando il ponte, perché non rispettare la loro scelta? Infine, 
  come possiamo avere fiducia in una persona che, invece di cercare di migliorare 
  la sua passerella, passa tutto il suo tempo a criticare il ponte?».</p>
<p>(Da una storia 
  raccontatami da Silvio Paulo Albino) Paulo Coelho (Quarto d’una serie di brevi 
  testi inediti dello scrittore brasiliano. I primi tre sono stati pubblicati 
  il 30 luglio, il 6 e il 13 agosto)</p></div><br>

