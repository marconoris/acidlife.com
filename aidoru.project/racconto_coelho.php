<p>Un monaco viveva 
  nelle vicinanze del tempio di Shiva. Nella casa di fronte, viveva una prostituta. 
  Osservando la quantità di uomini che andavano da lei, il monaco pensò di parlarle. 
  «Tu sei una grande peccatrice - la riprese -, irriverente verso Dio tutti i 
  giorni, e tutte le notti. E’ possibile che tu non riesca a fermarti, per riflettere 
  sulla vita dopo la morte?». La povera donna fu scossa dalle parole del monaco; 
  con sincero pentimento pregò Dio, implorando perdono. Tuttavia, chiese all’Onnipotente 
  di farle trovare una nuova fonte di guadagno. Ma non trovò nessun altro lavoro. 
  E, dopo una settimana di fame, tornò a prostituirsi. Ma, ogni volta che si concedeva 
  ad un estraneo, pregava il Signore, chiedendo perdono. Il monaco, irritato perché 
  il suo consiglio non aveva prodotto nessun effetto sulla prostituta, pensò tra 
  sé e sé: «A partire da ora conterò quanti uomini entreranno in quella casa fino 
  al giorno della morte della peccatrice». E da quel giorno, il monaco non fece 
  altro che osservare la routine della prostituta: ad ogni uomo che entrava, collocava 
  una pietra su un monte. Passato un po' di tempo, il monaco decise di parlare 
  di nuovo alla prostituta. Le disse: «Vedi questo monte? Ognuna di queste pietre 
  rappresenta uno dei peccati mortali che hai commesso, proprio dopo i miei ammonimenti. 
  Ora, torno a dire: pensa alle brutte azioni che stai commettendo!». La donna 
  incominciò a tremare, rendendosi conto quanto aumentassero i suoi peccati. Tornando 
  verso casa, sparse lacrime di sincero pentimento, pregando: «O Signore, quando 
  la Vostra misericordia mi libererà dalla vita infame che conduco?». La sua preghiera 
  fu ascoltata. Quello stesso giorno, l'angelo della morte passò da casa sua e 
  se la portò via. Per volontà di Dio, l'angelo attraversò la strada e portò via 
  con sé anche il monaco. L'anima della prostituta salì immediatamente in Cielo, 
  mentre i demoni portarono il monaco all'Inferno. Il monaco vide ciò che stava 
  accadendo ed esclamò: «O Signore, è questa la Tua giustizia? Io, che ho vissuto 
  con devozione e in povertà, sono ora condotto all'Inferno; mentre questa prostituta, 
  che ha vissuto costantemente nel peccato, sta salendo in Cielo!». Sentendo queste 
  parole, uno degli angeli rispose: «Sono sempre giusti i disegni di Dio. Tu ritenevi 
  che l'amore di Dio si riassumesse nel giudicare il comportamento del prossimo. 
  Mentre tu riempivi il tuo cuore con l'impurità dei peccati altrui, questa donna 
  pregava intensamente giorno e notte. L'anima della donna risultava così leggera 
  dopo il suo pianto che abbiamo potuto portarla fino in Paradiso. La tua anima 
  è carica di pietre e non siamo riusciti a farla salire così in alto».</p>
