<div class="ver2"><p>da la stampa on 
  line: <b><h4>Alfabeto del silenzio</b></h4></p>
<p>"Assenza di suoni", 
  "oblio", "condizione di chi non parla", e dunque omertà, indifferenza, perdita 
  di desiderio e passioni, venir meno della notorietà, ma anche "interruzione 
  di corrispondenza epistolare" e fors'anche, di questi tempi, telematica, "pratica 
  ascetica" insieme all'assai meno ascetico segnale che annuncia il sonno in caserma 
  (ma s'usa ancora, quella lunga, interminabile nota di tromba che dice, anzi 
  comanda: luci spente e quiete assoluta?). A questo triste per non dire deprimente 
  catalogo di significati, rimediano i "verdi silenzi" di D'Annunzio, che con 
  la loro fugace nota di colore evocano regioni di quiete intatta ma soprattutto 
  ritoccano la grigia monotonia dei dizionari intorno alla parola "Silenzio". 
  Non meno deludente è un viaggio nel frasario odierno in cerca di questa parola 
  tanto incompresa, a incominciare dai titoli dei libri, in cui tutto sembra esaurirsi 
  in un abuso di controsensi: abbondano infatti echi, parole, voci, persino grida 
  dal e del silenzio. Abbondano, per l'appunto: seguendo il filo d'Arianna di 
  uno dei tanti motori di ricerca del labirinto di internet, si troveranno nientemeno 
  che ventottomila trecento e trentadue documenti in cui compare la parola "silenzio", 
  un invito a nozze insomma per il paradosso. In altre parole, di silenzio si 
  parla eccome, e una rapida rassegna dei siti coinvolti conduce nei meandri della 
  politica (ma soprattutto di una retorica politica che non passa mai di moda), 
  in mezzo alla musica (piena di silenzi a quanto pare), fra i templi veri e virtuali 
  del New Age (dove il silenzio è, naturalmente, un must da prendersi però in 
  dosi omeopatiche), in sofferti dibattiti intorno al silenzio-assenso sull'espianto 
  degli organi. Eppure, al di là di un'apatia irrimediabile, oltre gli stereotipi 
  di una comunicazione di massa che fa immancabilmente del suo meglio per spogliare 
  le parole della loro energia naturale, il silenzio ha molto da esprimere. Esso 
  non è necessariamente un tacere, anzi, come ha detto in latino Ovidio, "spesso 
  tacendo, il volto parla". D'altro canto, se in italiano v'è una sola parola 
  per chiamare il silenzio, in altre lingue esso dispone di un vero e proprio 
  lessico: l'ebraico, ad esempio, ne conosce almeno quattro, che sanno di volta 
  in volta indicare l'abisso sordo che non ascolta, la serenità che invita a una 
  momentanea sospensione del dialogo, la complicità che non richiede parole per 
  spiegarsi, e infine quel silenzio che è soprattutto attesa. Perché allora non 
  provare a esplorare il silenzio, in cerca di ciò che si cela dietro la negazione 
  della parola e l'assenza di suoni? Ecco che, per l'ennesimo paradosso, sarà 
  proprio l'alfabeto a dare voci al silenzio, e dimostrare che spesso tacendo 
  si comunica.</p>
<p>ABISSO: un suono 
  che si propaga inutilmente fra sorde pareti, e malgrado un'eco eterna non giunge 
  mai ad alcuna destinazione. E' il silenzio che sa di un'assenza irrimediabile, 
  quella dei morti senza nome e senza storia, ad esempio, ma anche il quotidiano 
  smettere di amarsi, non aver più nulla da dirsi: ogni parola diventa un rimbombo 
  vano fra pareti di gomma, che tutto divorano. E' il grigiore, sì, ma della disperazione.</p>
<p>BENEFICIO: il 
  digiuno della parola come medicina dell'anima e del corpo è una delle pratiche 
  yoga fra le più salutari - e facili da rispettare: si potrebbe cominciare spegnendo 
  la televisione a settimane alterne, soltanto per sentire come l'aria, dentro 
  casa, si fa più trasparente, meno densa. La fase successiva potrebbe essere 
  quella di ascoltare il proprio respiro, qualche minuto al giorno appena: un 
  modo come un altro per sentirsi vivi, e magari rifletterci un po' sopra.</p>
<p>CANTO: Il canto 
  delle sirene era silenzio, null'altro che silenzio, ci dice Kafka, aggiungendo 
  che Ulisse forse lo sapeva e forse no. Il mito racconta che con strategica accortezza 
  l'eroe si riempì le orecchie di cera e come se non bastasse si fece incatenare 
  all'albero maestro, quando dovette passare nei paraggi dell'isola dalla fatale 
  attrazione. Misure inutili, se le sirene avessero cantato: "la passione di coloro 
  che venivano sedotti avrebbe spezzato ben altro". E invece loro tacquero, regalando 
  al viaggiatore l'illusione di un trionfo: ma la muta compiacenza delle sirene 
  ha tutto il sapore della beffa.</p>
<p>DIDONE: Nella 
  notte "silente" che annuncia l'estremo gesto della regina abbandonata da Enea, 
  sulla cui spada la mattina si getterà, "taceva l'aria intorno", racconta Virgilio. 
  Questo silenzio che accompagna il sonno del mondo non fa che accrescere l'affanno 
  della donna, rinfocolare l'amore perduto appena da poche ore, ma per sempre: 
  è lo specchio di un tormento interiore che ha i colori del deserto.</p>
<p>ELOGIO: Il silenzio 
  è talvolta misura provvidenziale, per non dire un desiderio sfrenato. Per neutralizzare 
  pedanti cultori del luogo comune in treno o in sala d'attesa dal medico, perché 
  non tirare fuori dalla borsetta e darsi a leggere l'opportuno Arte di tacere 
  di tal Dinouart (Sellerio, 1989, £ 10.000), che non mancherà di spiazzare la 
  più garrula compagna di scompartimento, il più tenace nostalgico dei tempi andati, 
  l'amministratore delegato con tre telefonini al seguito?</p>
<p>FORMULA: Chi tace 
  acconsente. L'equazione non vale sempre, tanto che l'originale latino di questo 
  adagio assai comune, opta per toni assai più possibilisti: "Qui tacet consentire 
  videtur", cioè "sembra che acconsenta". Quante volte, invece, è proprio il contrario: 
  il silenzio sa essere anche vibrata protesta, quando non ostruzionismo vero 
  e proprio. Scegliamo il silenzio per negare, e tuttavia tacendo diciamo della 
  rassegnazione a una battaglia che si sa perduta in partenza.</p>
<p>GRANDI PASSIONI: 
  Come poteva mancare Leopardi, in un lessico del silenzio? "Il silenzio è il 
  linguaggio di tutte le forti passioni, dell'amore (anche nei momenti dolci) 
  dell'ira, della maraviglia, del timore ec.", scrive nel 1820 fra le pagine dello 
  Zibaldone. Quanto umano sentire, davvero, riposa in questa sua considerazione 
  e non tanto in quel che è detto, ma soprattutto in quell'eccetera che apre spazi 
  sconfinati. Inoltre, nel marasma di parole e immagini che rappresenta il nostro 
  orizzonte quotidiano, sapere che tutto è ancora da dire perché sta dentro il 
  tacere, un po' consola e un po' dispera.</p>
<p>IMPERATIVO: Tocca 
  qui a un altro poeta, per il quale il silenzio è un comando, una strada cieca. 
  Paul Celan si getta nelle acque della Senna in un giorno imprecisato intorno 
  al venti di aprile del 1970. Era nato cinquant'anni prima a Czernowitz, in Ucraina. 
  "Silenzio! Io pianto la spina nel tuo cuore, poiché la rosa, la rosa sta con 
  le ombre nello specchio, e sanguina!". Anche il suo è un canto di passione e 
  d'amore.</p>
<p>LEVITAZIONE: Pesano, 
  le parole, e quanto a volte. Per sentirsi più leggeri, per alzarsi in volo, 
  non resta allora che tacere. "Andare verso Dio è stare in silenzio", ha detto 
  un eremita egiziano nell'antichità. Tacere diventa comunque un sollievo, un 
  levarsi pacato verso l'alto per prendere le distanze da ciò che ci circonda, 
  provare a guardare tutto e tutti come fossimo lassù.</p>
<p>MENZOGNA: Detta 
  una bugia, dopo conviene tacere. O rassegnarsi a mentire ancora, per non sconfessare, 
  contraddirsi, cadere in fallo. Il silenzio è la via obbligata di chi ha mentito 
  per errore e non per vocazione, come i bambini. Ma sta di fatto che il più delle 
  volte non siamo capaci di leggere i loro silenzi, semplicemente perché abbiamo 
  dimenticato quella stagione di vita in cui erano anche nostri.</p>
<p>NOSTALGIA: Dolore 
  del ricordo è la nostalgia, ma un dolore che spesso si sfoga senza parole e 
  anche senza gesti. E' il silenzio dell'esilio, di una lontananza così irreperabile 
  che non svanisce nemmeno quando si torna là donde s'era partiti. L'esilio è 
  una malattia che si porta dentro, senza sintomi esteriori né rimedio, se non 
  un silenzio che è blocco muto, come un sasso sopra il cuore.</p>
<p>OBLIO: Naturalmente, 
  il silenzio significa anche questo. Anche l'oblio può essere una medicina. Siamo 
  fatti di memorie non meno che di rimozioni, perché altrimenti, il peso dei ricordi 
  sarebbe insostenibile come l'esistenza di quel personaggio di Borges incapace 
  di dimenticare: un tormento ogni istante di vita. Il silenzio del dimenticare 
  è come una pausa nel tempo, per prendere fiato.</p>
<p>PARLARE AI POSTERI?: 
  A negare la necessità dell'oblio provvede subito un altro poeta, il Foscolo, 
  nel suo invito al giovane amico Pindemonte ad attingere coscienza dalle mute 
  tombe degli uomini celebri. Ma il silenzio dei sepolcri aiuta davvero a diventare, 
  o a sentirsi migliori? Che cosa dicono ancora, che non hanno detto in vita, 
  le spoglie mortali di poeti e condottieri? La fiducia nell'umana capacità di 
  superare la morte è proprio un sogno da poeti d'altri tempi.</p>
<p>QUIETE: Eccolo, 
  il silenzio che è pace, momentanea serenità. Quello che invoca la professoressa 
  dopo quattro ore di lezione a orde di sciamannati, la figlia dopo l'ennesima 
  predica della madre (ebbene sì, c'è qualche madre che ancora fa le prediche 
  alla figlia...), l'operaio dopo il turno alla catena di montaggio, l'omino dietro 
  il vetro della stazione di pedaggio autostradale. E quanti altri ancora, molestati 
  da barriere sonore violate, da parole di troppo. Il silenzio è spesso un traguardo 
  raggiunto con immane fatica e un desiderio che non s'estingue.</p>
<p>RIVENDICARE: Già, 
  anche in silenzio si può dire "voglio". Lo dicono gli occhi, lo sguardo che 
  s'abbassa, un fremito a fior di labbra che s'arresta sull'angolo della bocca. 
  Quante volte il "voglio" si ferma a mezza strada fra la testa e le corde vocali: 
  nelle masse mute di bambini del Terzo Mondo affamati e assediati dalle mosche, 
  nel passo stanco di chi è da sempre in cammino perché ovunque rinnegato.</p>
<p>SUONO SOTTILE: 
  così il profeta Elia chiama quel silenzio che è il vero momento della rivelazione 
  divina: un Dio, ci dice, che non era nel tuono, nella tempesta, nel vento furioso, 
  nel tremito della terra, era invece in una voce senza rumore, in un silenzio 
  che per vie misteriose giunge all'orecchio e di lì penetra nelle fibre più intime 
  che possediamo. Rivelazioni divine a parte, sono tanti i suoni sottili che al 
  solo volerlo, potremmo ascoltare.</p>
<p>TRACCIA: "La traccia 
  che il silenzio intaglia nell'anima", scrive Giulio Busi nel suo Simboli del 
  pensiero ebraico, (Einaudi 1999). I silenzi possono essere enigmi: quante volte 
  capita, infatti, di domandarsi il perché qualcuno taccia, invece di parlare. 
  A volte è solo questione di tempo, più spesso il silenzio è un segno che si 
  affida agli altri, nella speranza (ma anche nel timore) che riescano a decifrarlo.</p>
<p>ULTRASUONI: fra 
  le tante teorie sulle origini del cosmo, ve n'è una che per sommi capi dice 
  più o meno così: l'alba del creato echeggia ancora in microonde sonore che qualche 
  astrofisico ha per caso intercettato di recente. Sono vibrazioni che ci giungono 
  dagli estremi dell'universo, e siccome la distanza nello spazio è anche corsa 
  nel tempo, esse provengono anche dai primordi. Ecco, se c'è un silenzio che 
  mi piacerebbe spezzare, è quello che ci impedisce di udire queste risonanze 
  con le nostre orecchie.</p>
<p>VIZIO: "Tra i 
  vizi più strani e gravi della nostra epoca, va menzionato il silenzio. Strappate 
  dolorosamente al silenzio, vengono fuori le poche, sterili parole della nostra 
  epoca, come segnali di naufraghi, fuochi accesi tra colline lontanissime, flebili 
  e disperati richiami che inghiotte lo spazio". Natalia Ginzburg è arrabbiata 
  con il silenzio capace di partorire solo poche parole monche: lo considera una 
  ricchezza fasulla.</p>
<p>ZITTE!: Non poteva 
  che finire così. Eugenio Montale, invece, è arrabbiato con le parole: "Non chiederci 
  la parola che squadri da ogni lato l'animo nostro informe, e a lettere di fuoco 
  lo dichiari e risplenda come un croco perduto in mezzo a un polveroso prato".</p>
