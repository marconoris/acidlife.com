<div class="ver2"><p><h4>In libreria un libro contro la pena di morte</h4></p>

<p>Da luglio e' in vendita nelle migliori librerie 
"Il Tritacarne - una voce dal carcere di Florence, Arizona" 
di Karl Luis Guillen 
presentazione di Marco De Ponte, vicepresidente di Amnesty International  
prefazione di Massimo Carlotto
L. 12000 
Edizioni Multimage</p>

<p>In dodici anni di prigione di massima sicurezza, innocente, sotto la minaccia 
di una condanna a morte, l'autore non ha mai perso la speranza di un giusto
processo ed ha mantenuta viva la sua fiducia, la sua umanita' e la sua
gioia di vivere. 
In questo libro, un romanzo intenso come un film d'azione, racconta della 
assurda vicenda che lo ha portato, a ventanni, in un carcere americano 
vittima dell'iniquo sistema giudiziario americano; dei meccanismi interni della 
prigione: gang, violenze, controlli, privazioni; della condanna a morte e del 
florido business della privatizzazione delle carceri usato come metodo 
politico di controllo sociale. 
La casa editrice umanista Multimage ha pubblicato questo libro, i cui 
proventi vanno integralmente all'autore per garantirgli la difesa legale, oltre 
che per l'intriseca qualita' della scrittura, per aiutare il Comitato per la Difesa
di Karl Guillen, che e' presente in 15 citta' italiane e che in un anno di
azione militante e' riuscito a venderne duemila copie. Grazie
all'attenzione del Comitato la condanna a morte per Karl Guillen e' stata
scongiurata e comminata una pena detentiva di venti anni. La vendita del
libro continua per permettere all'autore una revisione dei processi per il
riconoscimento della sua totale innocenza e come prezioso strumento di
testimonianza e denuncia per tutti coloro che come noi credono che
giustizia significhi dare a ciuascuno la possibilita' di difendersi e che
considerano la pena di morte un barbaro crimine indegno di una societa'
civile. 
Tutte le info della campagna ed estratti del libro sono consultabili su internet 
al sito:
<a href="http://www.pegacity.it/cultura/sistampi/karl" >http://www.pegacity.it/cultura/sistampi/karl</a></p>

