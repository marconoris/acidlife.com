From: Information Guerrilla <redazione@informationguerrilla.org></p>


<div class="legg">Se vogliono che gli tiriamo le castagne fuori dal fuoco ci diano un mandato. E un assegno!
Sono stato due giorni a pensare sulla questione degli ostaggi. Due giorni a sentire le cariatidi parlanti di una sinistra impresentabile, pure nel salotto dell'untuoso Vespa. Due giorni a pensare. E sono arrivato alla conclusione: il movimento (parliamo oggi del movimento per la pace) ha tre nemici. E c'e' una trappola: pronta a scattare. Il primo nemico e' facilmente identificabile, le tre B, Bush, Blair & Berlusconi. Chi ha scelto la via della "Guerra fra le civilta'" ,con conseguenze ormai difficilmente controllabili (movimenti separatisti mussulmani in Thailandia, e' notizia di oggi!).
Il mondo nei prossimi 50 anni almeno vedra' una guerra "infinita", a volte fatta di conflitti aperti, a volte di stragi d'innocenti, a volte di bombe che esplodendo renderanno sempre piu' difficile capire da che parte provengano. Il secondo nemico non e' facilmente identificabile. Il termine "terrorismo" e Guerra al terrorismo non spiegano niente. Il terrorismo e' una tattica militare, non una posizione politica. Oggi sotto il termine "terroristi" in Irak vanno a finire sia i maledetti criminali che hanno fatto l'attentato a Bassora, quelli della sinangoga di Instabul, quelli che hanno bombordato la croce rossa, le milizie scite di Al Sadr, i resistenti di Falluja, gli ex bathisti e dio solo sa chi altro. Non escluderei che qualcuno di questi attentati siano abili operazioni di "milizie private" pagate in dollari. Ma e' identificabile un progetto politico: riunificare i movimenti di opposizione al capitalismo neo-liberale sotto la bandiera verde del Profeta. I "dannati della terra" guidati da miliardari sauditi, o da preti sciti. Uno scenario da incubo. Non facciamoci ingannare dalla propraganda occidentale, limitandoci a rovesciare le affermazioni, come se la politica si potesse fare con il gioco del "buono" e del "cattivo". Il rischio dell'integralismo esiste. In Oriente come in Occidente. Nelle sette protestanti WASP di Yale come nei palazzi di Rihad, nelle caserme pakistane, come nelle banche islamiche. Che poi tutti questi seguaci dell' "unico Dio" si ritrovino nei consigli di amministrazione di Citygroup o di Carlyle dovrebbe farci un po' piu' scettici.
Sauditi e Americani hanno deciso insieme l'attacco a Saddam. C'e' un'oggettiva coincidenza di interesse fra chi vuole crearsi "il nuovo nemico dopo la guerra Fredda" (Kissinger) e chi aspira alla conquista islamica del terzo mondo. Non sara' politicamente corretto dirlo, ma e' cosi'.
Il terzo nemico e' la ruota di scorta del neo-liberismo, la sinistra italiana dei D'Alema, Prodi e Bersani, ma anche di Blair: quelli che devono "farsi carico" delle bandiere liberali stracciate dai monopolisti come Berlusconi. Cretini. Ma essi stessi pericolosi. Sullo sfondo le "coorporation" che non hanno ne politica ne' religione, ma che conoscendo bene i propri interessi sfruttano gli idioti di turno, a volte li eleggono direttamente, a volte li finanziano, spesso li armano.
Ora ci chiedono di schierarci. Tutti sarebbero contenti. Ognuno al suo posto. I disobbedienti con gli islamici, i DS fra governo e movimenti, gli anticapitalisti islamici che danno indicazioni agli anticapitalisti italiani. Uno scenario "alla Pasquinelli" - che va subito liberato perche' in Italia non e' previsto il reato di "idiozia" - senno le carceri scoppierebbero.
E tutto per tre "soldati di ventura", se vogliamo evitare il termine "mercenari". Tre disgraziati finiti in mezzo alle inefficienze dei servizi segreti, alle strategie mediatiche di un Governo vergognoso, alle farneticazioni di Barbara Contini che annuncia di aver pagato il riscatto prima della liberazione degli ostaggi.
Noi dovremmo togliergli le castagne dal fuoco, e per di piu' fare la parte dei filo-islamici-terroristi ecc. ecc.
Ho sentito dire che questa situazione potrebbe prendere una deriva spagnola, con la piazza che caccia Berlusconi. E' una tesi generosa, ma ahimé priva di senso politico. Primo non abbiamo elezioni politiche in corso, solo le Europee. Due, in Spagna hanno cacciato un Presidente che mentiva. Qui l'idiota invece dice anche troppo chiaramente da che parte sta e cosa vuol fare in Irak. e ricordatevi Marx quando diceva che la storia si ripete due volte, la prima come dramma, la seconda come farsa.
E allora?
Allora niente. Si continua a fare quello che riusciamo a fare. Mettendo sempre piu' intelligenza nell'azione politica. Cercando di ingradire il movimento della pace fino a delegittimare tutti i "governi di guerra", anche quelli che hanno fatto la guerra a Belgrado.
Strada lunga, difficile ma senza scorciatoie.
E se proprio vogliono che gli tiriamo le castagne fuori dal fuoco, abbiano almeno il buon gusto di un gesto forte.
Un mandato governativo per manifestare, con allegato assegno e rimborso spese.

