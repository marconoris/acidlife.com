<div class="tw_comm"><p>Da <a href="http://punto-informatico.it/p.asp?i=48137" target="new">www.punto-informatico.it</a></p>

<p>La prima viene indetta in tutte le città italiane mentre il 18 si terrà<br />
davanti a Palazzo Madama, sede del Senato. Ecco cosa chiedono gli<br />
organizzatori della contestazione.<br />
</p>


<div class="legg">11/05/04 - News - Roma - I Verdi con il senatore Fiorello Cortiana, che si batte da settimane contro il DL Urbani e le sue successive modifiche, hanno già dichiarato la propria adesione ad una manifestazione che
gruppi di utenti internet stanno organizzando nelle città italiane e a Roma. Una contestazione che si terrà in due tappe, il 15 e il 18 maggio, e che intende portare nelle piazze il dissenso contro il provvedimento su cinema e pirateria che ha scatenato vivacissime polemiche in rete (punto-informatico.it/p.asp?i=48014).

<p>Come si può leggere su uno dei siti della protesta (www.mannasauro.com/nourbani.html) il 15 maggio si sta organizzando una azione di volantinaggio per sensibilizzare l'opinione pubblica e per informare della dimostrazione prevista per martedì 18 maggio (giorno in cui si discuterà al Senato del DL Urbani) davanti Palazzo Madama a Roma.</p>

<p>Il volantino della protesta ( qui (www.mannasauro.com/volantino.pdf) in formato.pdf) non usa mezzi termini per raccontare la questione:</p>

<p>"Da oggi andare in carcere è ancora più facile / Soprattutto per i vostri figli<br />
Un altro impegno mantenuto / più tasse sui masterizzatori, hard disk, CD, DVD, videocassette e qualsiasi prodotto e supporto per la registrazione di dati, video e audio".</p>

<p>E insiste:</p>

<p>"Vostro figlio scarica una canzone del suo cantante preferito da Internet? Attenti perchè potreste ritrovarvi con le guardie di finanza a casa!<br />
Ora si rischiano sanzioni fino a 4 anni di reclusione, una multa fino a 15.493 euro, confisca delle apparecchiature informatiche (altre migliaia di euro), la pubblicazione su uno o più giornali a tiratura nazionale.<br />
È QUESTO IL FUTURO DELL'ITALIA? CI ASPETTANO ANNI DI PROIBIZIONISMO?<br />
Secondo i nostri governanti i maltrattamenti in famiglia o verso i fanciulli (art 572) la violenza privata (art 610) e lo scambio di files su internet senza fini di lucro devono essere puniti allo stesso modo".</p>

<p>Ieri intanto lo stesso Cortiana ha ricordato che se il 18 al Senato verrà approvato il provvedimento di conversione "si otterranno tre effetti: una criminalizzazione di massa degli utenti che condividono contenuti, film e musica, che rischieranno sanzioni fino a 4 anni di reclusione e una multa fino a 15.493 euro; il blocco del sistema delle comunicazioni digitali, con l'introduzione di un bollino SIAE per tutto cio' che viene trasmesso in rete; l'aumento spropositato dei costi dell'hardware, con tasse che per un hard disk da 120 GB possono arrivare a 40 euro". Cortiana ha spiegato che i Verdi aderiscono "convinti alle<br />
mobilitazioni del popolo della rete contro questo obbrobrio, per la libertà e per una legge seria che regoli la proprietà intellettuale".</div></p></div>
