<div class="tw_title"><a name="000013">Iraq: Fermiamo il massacro di Falluja</a></div>
<div class="tw_comm">
<p>De:   info-unponteperXX@XXunimondo.org<br />
Asunto: [info-unponteper] Iraq: Fermiamo il massacro di Falluja<br />
Fecha: 29 de abril de 2004 16:53:58 GMT+02:00</p>

<p>Siamo ormai al ventitreesimo giorno dell'assedio di Falluja, iniziato il 6 aprile quando le truppe statunitensi hanno circondato la città chiudendo con muri di sabbia tutte le vie di accesso e lanciando un attacco militare dalla terra e dal cielo...</p>

<div class="legg">Siamo ormai al ventitreesimo giorno dell'assedio di Falluja, iniziato il 6 aprile quando le truppe statunitensi hanno circondato la città chiudendo con muri di sabbia tutte le vie di accesso e lanciando un attacco militare dalla terra e dal cielo.
Centinaia i civili già morti, comprese donne e bambini; decine di migliaia di persone hanno dovuto lasciare la città ed ora si trovano ospitati in ricoveri di fortuna a Baghdad e in tutta l'area circostante.
E' in corso una "punizione collettiva" della popolazione della città, come è stata definita da esponenti dello stesso Consiglio di Governo iracheno, per aver resistito alla occupazione militare statunitense.

<p>A Falluja sono state violate tutte le convenzioni internazionali<br />
A Falluja è stato negata la istituzione di zone di sicurezza e di corridoi umanitari per soccorrere i feriti ed inviare aiuti<br />
A Falluja è stato impedito ai feriti di raggiungere l'ospedale cittadino<br />
A Falluja cecchini statunitensi hanno sparato sulle ambulanze, su persone che entravano ed uscivano dagli ospedali, su persone con le mani alzate o che brandivano bandiere bianche.<br />
A Falluja è stato impedito alla popolazione in fuga di uscire dalla città<br />
A Falluja sono state utilizzate bombe a grappolo e negli ultimi giorni sono stati effettuati più bombardamenti aerei su zone civili.</p>

<p>Tutto ciò è stato fatto dai "nostri migliori amici", senza che nemmeno una protesta si sia levata da parte del nostro governo.</p>

<p><br />
Chiediamo che il Governo e le forze politiche italiane condannino con forza il massacro di Falluja e si attivino per evitare che accada la stessa cosa nella città santa di Najaf. </p>

<p>Chiediamo che il Governo e le forze politiche italiane sostengano la richiesta del Consiglio Cittadino della città di Falluja per un intervento urgente del Segretario Generale dell'Onu.<br />
Invitiamo la società civile italiana e il movimento per la pace a mobilitarsi con urgenza</p>

