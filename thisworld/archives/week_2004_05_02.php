<div class="tw_comm">
<p><span style="width:320;font-size:12px;line-height:98%;font-family:verdana;color:#666666;font-weight:bold;"><a title="Media Activism | OURMedia conferenza a Porto Alegre- call aperta" href="http://www.rekombinant.org/media-activism/article.php?sid=138">Media Activism | OURMedia conferenza a Porto Alegre- call aperta</a></p>

<p>OURMedia è una rete internazionale di più di 270 ricercatori e attivisti che lavorano in più di quaranta paesi per promuovere i media e le tecnologie che supportano la giustizia sociale, i diritti umani, l'autonomia delle comunità e i movimenti. La prossima conferenza è a Porto Alegre a luglio. La call per paper e proposte è aperta fino al 10 maggio</span></p>

<div class="legg">OURMedia/NuestrosMedios IV: Building Communication Societies
An International Conference
July 22-25, 2004 Porto Alegre, Brazil

<p>:::: CONFERENCE ANNOUNCEMENT AND CALL FOR PROPOSALS ::::</p>

<p>Proposal Deadline: May 10, 2004, 22:00/10pm GMT (17:00/5pm EDT).</p>

<p>OURMedia/NuestrosMedios is an international network of more than 270<br />
academics and activists: researchers, practitioners and advocates<br />
working in more than forty countries to strengthen media and<br />
information/communication technologies (ICTs) that support social<br />
justice, human rights, community autonomy and grassroots empowerment.</p>

<p>Our work spans many fields: community media, independent media, radical<br />
media, citizens media, grassroots networking, telecommunications<br />
policy, indymedia activism, cultural arts, communications theory,<br />
social-movement research, sociology, anthropology, cultural studies,<br />
development communication and communication for social change.</p>

<p>Our annual meeting is a time for OURMedia members to learn from<br />
each other and to strengthen our analyses, strategies, collaborations<br />
and campaigns. At this point, membership is free and open to anyone who<br />
shares our guiding principles: please see our website: http://www.ourmedianet.org.</p>

<p>We invite you to join us (membership is included with conference registration).</p>

<p>OURMedia/NuestrosMedios IV: “Building Communication Societies” will take place in<br />
Porto Alegre, Brazil, July 22-25, 2004 as a pre-conference to the IAMCR,<br />
the International Association of Media and Communications Research,<br />
but is independent from that event, with our own program, specific criteria<br />
for session proposals and registration fees (scaled to the participant's ability to pay,<br />
no-one will be turned away for lack of funds).</p>

<p>Please see http://www.ourmedianet.org/om2004 for more information.</p>

<p>This four day conference will feature:</p>

<p>++ FIELD TRIPS to local Brazilian grassroots media and ICT projects,<br />
advocacy campaigns and popular education centers</p>

<p>++ PRESENTATIONS AND PARTICIPATORY SESSIONS: panels, workshops and<br />
strategic discussions to focus on connecting research, advocacy and practice.<br />
Topics include:<br />
** Connecting Research and Advocacy for Citizens’ Media, Community Media<br />
** Current Media/ICT Policy Issues and Implications.<br />
** "Best Practices" and "Notable Failures," (stories and lessons from<br />
citizens' media and grassroots networking projects, media/ICT advocacy<br />
and policy campaigns, researcher-practitioner collaborations and other<br />
social movements that relate to current efforts for community/citizens<br />
media, "communication rights," "media democracy,” "free speech," "media<br />
diversity," "media justice" and other goals.)</p>

<p>++ POLYMEDIA LABORATORY: "NossoMidiaLab" -- an open space for experimentation,<br />
creation, collaboration and skill-sharing to explore autonomous and community uses<br />
of video, audio, Internet and other traditional and new media technologies. Help create it:<br />
http://docs.indymedia.org/view/Global/NossoMidiaChamada</p>

<p>A Call for Proposals is currently open until May 10. You do not have<br />
to submit a proposal to attend the conference. For more information on<br />
registering to attend or submitting a proposal, please visit our web<br />
page or write to om2004@ourmedianet.org to request a copy of<br />
the Call for Proposals or the Registration Form.</p>

