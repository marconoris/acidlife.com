<div class="tw_title">630.000 litri d'acqua a settimana per gli abitanti di Falluja</div>
<div class="tw_comm"><p>Continuano gli interventi di aiuto di Un Ponte per… a favore dei cittadini di Falluja.</p></div>
<div class="legg">Dal 6 di maggio 3 autobotti dell’associazione Un Ponte per… portano acqua agli abitanti di Falluja.

<p>Ogni mattina partono da Baghdad 3 camion contenenti 45 mila litri di acqua. Terminata una prima distribuzione, i serbatoi vengono nuovamente riempiti presso la fonte di acqua potabile di Abu Ghraib per la seconda distribuzione giornaliera.</p>

<p>L’intervento d’emergenza comprende anche la fornitura di contenitori agli abitanti per evitare possibili contaminazioni dell’acqua potabile.</p>

<p>I 90.000 litri di acqua potabile al giorno sono stati per ora indirizzati alle zone maggiormente colpite dal conflitto: i villaggi di Jolan, nell’area industriale di Falluja (Al-Askary), a Naimia (dove sono<br />
presenti ancora molti sfollati), ed il centro della città di Falluja.</p>

<p>La situazione idrica a Falluja è molto critica a causa dei recenti bombardamenti americani che hanno distrutto una parte consistente della rete di distribuzione.<br />
La principale centrale di potabilizzazione, che riforniva quasi l’intera città, riesce ora a soddisfare soltanto il 20% del fabbisogno dell’area. Anche il maggiore centro medico allestito a Falluja è senza acqua da giorni. </p>

<p>"I medici degli ospedali cittadini – afferma la capomissione di Un Ponte per… Simona Torretta, rientrata in Iraq nelle scorse ore da Amman – confermano l’aumento di malattie gastrointestinali in particolar modo tra i bambini, ma anche tra gli adulti in seguito alla mancanza d’acqua in città".</p>

<p>Per evitare un’ulteriore diffusione delle infezioni gastrointestinali, Un Ponte per… fornirà agli ospedali cittadini 3000 pasticche di cloro in modo da rendere le strutture in grado di sterilizzare autonomamente l’acqua.</p>

<p><br />
La distribuzione dell’acqua è svolta in collaborazione con la moschea di Al Zafaraniya di Baghdad.</p>

