<div class="tw_title">Farsi i detersivi in casa</div>
<div class="tw_comm"><p>Tratto da <a href="http://www.bilancidigiustizia.it/Sections-article25-p1.html" target="new">www.bilancidigiustizia.it/</a></p></div>
<div class="legg">Gli elementi "base" per la realizzazione di detersivi in casa sono la SODA, la SAPONATA, la POTASSA.

<p>SODA: carbonato di sodio, alcalina, se usata a caldo ha la proprietà di sciogliere lo sporco.</p>

<p>SAPONATA: 600 gr . di sapone di Marsiglia in 5 l. di acqua piovana (al posto dell’acqua distillata). Far bollire l’acqua e i pezzi di sapone finché non saranno completamente sciolti.</p>

<p>POTASSA: potassio carbonato 98-100% non di facile reperibilità in quanto viene utilizzata nei processi di lavorazione chimici e non dall’utente finale (tramite amici farmacisti p.e.). E’ più basica (potente) della soda.</p>

<p>SAPONE PER LE MANI: aggiungere alla saponata acqua quanto basta per diventare liquido. Finché è ancora tiepido aggiungere l’essenza preferita (limone, lavanda ecc.)</p>

<p>PER I PIDOCCHI VERDI: 100 gr. di saponata, 1 l. di acqua, 100 gr di olio di semi di arachide.</p>

<p>Sciogliere la saponata nell’acqua, quando è ancora tiepido aggiungere l’olio ed emulsionare energicamente. Si usa 1 dose di questo composto con 10 dosi di acqua in uno spruzzatore. Ottimo per le rose e i fagiolini. E’ inefficace contro i pidocchi neri.</p>

<p><br />
<b>DETERSIVO IN POLVERE</b></p>

<p><br />
Per capi resistenti molto sporchi:</p>

<p>Strofinare le macchie o le parti più sporche con la saponata (a freddo), mettere i panni nella lavatrice e aggiungere, direttamente nel cestello, un pugno di soda. Avviare la lavatrice per il carico dell’acqua quindi spegnerla e lasciare la biancheria in ammollo per alcune ore. In base alla durezza dell’acqua può variare in più o in meno la quantità di soda: bisogna provare.</p>

<p>Attenzione: la soda non va messa nella vaschetta del detersivo perché potrebbe "cementarsi" e arrivare a rovinare la lavatrice. Si potrebbe eventualmente far sciogliere la soda sbattendola in un vasetto con qualche cucchiaio di acqua calda e metterla nella lavatrice mentre sta caricando l’acqua.</p>

<p>Per capi resistenti normalmente sporchi:</p>

<p>1-1/2 kg di scaglie di sapone di Marsiglia, 750 gr. di soda, 2 cucchiai di acqua piovana (non indispensabile).</p>

<p>Mettere le scaglie di sapone e soda in un contenitore fornito di coperchio e mescolare con un grande mestolo di legno. Mentre mescolate spruzzarvi lentamente sopra l’acqua , finché la soda aderisce bene alle scaglie. In tal modo le scaglie di sapone e la soda non si separeranno nuovamente e il risultato del lavaggio si manterrà costante. (Dall’esperienza fatta risulta che si ottengono buoni risultati anche senza utilizzare l’acqua.)</p>

<p>Si conserva per circa 8 mesi.</p>

<p>Dosi: circa 1-1/2 cucchiai colmi per lavaggio.</p>

<p><b>DETERSIVO LIQUIDO</b></p>

<p><b>Per capi molto sporchi</b><br />
50 gr di saponata, 1200 ml di acqua piovana, 200 gr di potassa.</p>

<p>Mettere l’acqua in una pentola smaltata e riscaldarla lentamente. Aggiungere la potassa e scioglierla completamente. Versare le scaglie di sapone e scioglierle mescolando in continuazione, senza far bollire. Lasciar quindi raffreddare il detersivo. Mescolare un paio di volte e filtrare quindi attraverso un colino da cucina e versare in una bottiglia di plastica.</p>

<p>Si conserva per circa 3 mesi.</p>

<p>Dosi: circa 30 ml per lavatrice, per lavaggi a mano 10 ml in 10 l di acqua.</p>

<p><b>Per capi normalmente sporchi</b><br />
200 gr di saponata, 1200 ml di acqua piovana, 40 gr di potassa.</p>

<p>Mettere l’acqua in una pentola smaltata e riscaldarla lentamente. Aggiungere la potassa e scioglierla completamente. Versare le scaglie di sapone e scioglierle mescolando in continuazione, senza far bollire. Lasciar quindi raffreddare il detersivo. Mescolare un paio di volte e filtrare quindi attraverso un colino da cucina e versare in una bottiglia di plastica. Si conserva per circa 3 mesi.</p>

<p><br />
Dosi: circa 80 ml per lavatrice.</p>

<p><b>DETERSIVO PER LAVASTOVIGLIE E PER I PIATTI</b></p>

<p>200 gr sale grosso, 100 gr aceto bianco, 400 gr acqua, 3 limoni interi con buccia.</p>

<p>Mettere i limoni e il sale nel frullatore per 20 sec. Mettere sul fuoco e aggiungere l’acqua e l’aceto, far bollire per circa 15 min. sbattendo con una frusta. Conservare in barattolo di vetro.</p>

<p>Dosi: due cucchiai nella vaschetta della lavastoviglie o quanto basta sulla spugna per lavaggi a mano.</p>

<p>Il brillantante può essere sostituito con l’aceto e il sale per lavastoviglie con sale grosso da cucina.</p>

<p>Si può usare anche la cenere fatta bollire in acqua per 10 min. e filtrata.</p>

<p><b>DETERSIVO PER WC</b></p>

<p>25 gr. di acido citrico (succo di limone), 210 ml di acqua, 10 ml di detersivo per i stoviglie, 5 gocce di olio essenziale. al limone.</p>

<p><b>DECALCIFICANTE</b></p>

<p>Mezzo litro di aceto di mele e 3 cucchiai di acido citrico (succo di limone).</p>

<p><b>ALTRI UTILIZZI DELLA SODA</b></p>

<p>- Ottima per sgrassare, lavare , pulire, deodorare tutte le superfici lavabili come mobili laccati, finestre, tapparelle, pavimenti, lavello della cucina, sanitari del bagno, specchi. Risciacquare accuratamente con acqua corrente le superfici che andranno a contatto con gli alimenti o del bagno.</p>

<p>Dose: sciogliere 1 cucchiaio di soda in 5 l d’acqua calda. (aumentando fino a 2 cucchiai all’aumentare della durezza dell’acqua)</p>

<p>Non usare su alluminio, legno e cotto.</p>

<p>- Ottimo rimedio per pulire e togliere i depositi di vino dalle bottiglie. Riempire ogni bottiglia circa a metà con una soluzione di acqua tiepida e soda, quindi tappandola con una mano, agitarla vigorosamente fino a che la bottiglia non risulti perfettamente pulita. Gettare la soluzione sporca e sciacquare la bottiglia molto bene con acqua corrente quindi per asciugarla lasciarla scolare capovolta all’ombra.</p>

<p>Dosi: 30-40 gr di soda in 1 litro di acqua.</p>

<p><b>Vetri e specchi e pulizia rapida di piastrelle e superfici</b></p>

<p>Mettere in una bottiglia con spruzzatore un quarto di litro di acqua, mezzo bicchiere di aceto, mezzo bicchiere di alcool e tre gocce di liquido per stoviglie. Spruzzare il liquido, in piccola quantità, sulla superficie da pulire, quindi asciugare strofinando con un panno asciutto o con carta di giornale.</p>

<p><b>Pavimenti non porosi (piastrelle, parquet verniciato, marmo lucidato)</b></p>

<p>Versare nel secchio di acqua calda da mezzo a un cucchiaio di liquido per stoviglie e mezzo bicchiere di aceto bianco; per disinfettare aggiungere anche 5 gocce di olio essenziale di timo. Se il detergente è stato dosato correttamente, non sarà necessario risciacquare.</p>

<p><b>Pavimenti non porosi (cotto, graniglia, pietra)</b></p>

<p>Sapone di Marsiglia (strofinare il pezzo di sapone sullo straccio bagnato).</p>

<p><b>Sanitari, lavelli in acciaio o ceramica, formica, superfici smaltate</b></p>

<p>Sapone di Marsiglia (strofinarne un po' sullo straccetto); il risciacquo è facilissimo. Per sporco resistente (cappa, fornelli, sporco vecchio, piatto doccia) preparare una polvere detergente mescolando, in un barattolo di vetro con coperchio ermetico, 2 cucchiai di argilla bianca, un cucchiaio di pomice, un cucchiaio di soda e dieci gocce di olio essenziale di bergamotto. Si usa come una comune polvere abrasiva ed è ottima anche come detergente battericida per sanitari. Come anticalcare e lucidante usare aceto bianco.</p>

<p><b>Sapone universale</b></p>

<p>Ingredienti per 15 pezzi di sapone circa:<br />
5 litri di acqua<br />
1 litro di olio di oliva<br />
180 gr di soda caustica</p>

<p>Ottimo per tutti gli usi, dalla pelle delicata dei neonati al lavaggio della biancheria, dai piatti alle mani.<br />
Versare gli ingredienti in una pentola dalle pareti alte. Far cuocere per dieci ore circa a fuoco lento. Durante la cottura fare attenzione perché il contenuto, bollendo, può fuoriuscire; per impedire che ciò avvenga aggiungere un po' di acqua fredda. Il sapone è pronto quando, versandone un po' e facendolo raffreddare, diventa duro e liscio. Una volta raffreddato il sapone va tagliato in pezzi e fatto seccare. Sarà pronto in circa sei mesi.<br />
Per ottenere saponi a base di essenze di erbe e fiori aggiungere all'acqua utilizzata infusi di alloro, camomilla, lavanda, petali di rosa, malva, ortica, chicchi di avena.<br />
Questo sapone funziona benissimo anche per il lavaggio in lavatrice, mettendone direttamente un pezzo nel cestello dei panni.</div></p>


