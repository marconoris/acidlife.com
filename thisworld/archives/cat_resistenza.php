<div class="tw_title">Battisti: disinformazione sull'udienza di ieri</div>
<div class="tw_comm"><p>A cominciare da ieri sera, 12 maggio, per continuare stamattina, quasi tutti i media italiani hanno riportato notizie inesatte o incomplete sullo svolgimento dell'udienza, davanti alla Chambre d'Instruction di Parigi, in cui e' stata discussa la domanda di estradizione di Cesare Battisti richiesta dal governo italiano...</p></div>
<div class="legg">Da www.carmillaonline.com.

<p>A cominciare da ieri sera, 12 maggio, per continuare stamattina, quasi tutti i media italiani hanno riportato notizie inesatte o incomplete sullo svolgimento dell'udienza, davanti alla Chambre d'Instruction di Parigi, in cui e' stata discussa la domanda di estradizione di Cesare Battisti richiesta dal governo italiano.<br />
Si va da chi afferma che il tribunale avrebbe espresso parere favorevole all'estradizione (e magari ne esulta, come i "giornali" della catena Riffeser) a chi riporta solo le argomentazioni dell'accusa, quasi fossero state cosi' schiaccianti da non potere essere contrastate.<br />
Cio' si deve a un doppio equivoco. </p>

<p>Da un lato, sulle prime, alcune agenzie di stampa italiane hanno male interpretato la parola "Parquet", e creduto che indicasse l'intero tribunale. In realta', indica l'equivalente della nostra Procura della Repubblica, cioe' la pubblica accusa. Era ovvio che quest'ultima si pronunciasse favorevolmente all'estradizione, visto che è il suo compito istituzionale.<br />
D'altro lato, molti media si sono basati su un comunicato dell'Agenzia France Presse e su un articolo on line di Le Monde pubblicati verso le 20, mentre l'udienza era ancora in corso e prima dell'intervento conclusivo della difesa.<br />
E' stato al momento di quest'ultimo che si e' verificato un colpo di scena, con l'apparizione inattesa dell'avvocato milanese Giuseppe Pelazza, difensore di Cesare Battisti fin dal 1979. Questi ha fatto letteralmente a pezzi le argomentazioni del "pubblico ministero" Sylvie Petit-Leclair, che aveva asserito la possibilità per Battisti di avere un nuovo processo in Italia, e sostenuto la legittimita' della nuova richiesta italiana di estradizione perche' quella originaria si sarebbe basata su mandati di cattura e non sulla sentenza definitiva. L'avvocato Pelazza, nel ridicolizzare simili invenzioni, ha anche rievocato il clima in cui si svolgevano i processi in Italia ai tempi della cosiddetta "emergenza".<br />
Altro colpo di scena e' stato l'intervento dell'ex presidente dell'ordine parigino degli avvocati, Paul-Albert Iweins, a sostegno della denuncia dei legali di Battisti contro l'Ambasciata italiana a Parigi che, in una propria memoria, li aveva pesantemente insultati e aveva ingenuamente confessato un'intercettazione illegale di corrispondenza.<br />
La seduta si e' conclusa a tarda ora, quando tutti i media italiani avevano gia' cominciato a sparare cazzate, riflesse dai quotidiani di stamattina.<br />
La Chambre d'Instruction emettera' la propria sentenza il 30 giugno. (V.E.)</p>

