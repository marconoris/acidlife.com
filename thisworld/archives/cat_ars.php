<div class="tw_title">Non-linear correlations</div>
<div class="tw_comm"><p>La fabbrica<br />
via locarno, 43 - 6616 Losone<br />
Switzerland</p>

<p>COMUNICATO STAMPA/PRESS RELEASE<br />
inaugurazione/opening<br />
28 maggio/may 04<br />
h. 18.30</p>

<p>correlazioni non lineari/non-linear correlations<br />
a cura di/curator  Stefano Verri<br />
concept Riccardo Lisi</p></div>
<div class="legg">I cinque artisti premiati a diverso titolo dal Premio Migros per la Cultura in occasione della mostra “Che c’è di nuovo? La scena artistica emergente in ticino” tornano in “correlazioni non lineari” evento promosso dal centro culturale la fabbrica di Losone (TI).

<p>Cinque artisti stilisticamente e tecnicamente differenti, ticinesi, nati tra la fine degli anni Sessanta e l’inizio dei Settanta, figli dello stesso background culturale che hanno dato vita a poetiche creative proprie e personali.</p>

<p>Katia Bassanini attraverso una sagace ironia propone una personale visione del mondo, fatta di competizione e volontà di supremazia. Una poetica dell’assurdo che propone riflessioni profonde. Gian Paolo Minelli propone invece una serie di fotorgrafie scattate nel Carcel de Caseiros: una documentazione sociale che si fonda sulle tracce lasciate dai detenuti. Elementi sociali che tornano in Andrea Crociani che attraverso uno scritto propone una riflessione sull’anarchia, mentre con una proiezione di 36 diapositive esegue una “ricerca” sulla violenza come elemento dilagante nel mondo. Il duo Terzaghi/Zurcher indaga invece la possibilità di una nuova frontiera del linguaggio fatto di giustapposizioni coerenti di elementi di recupero.</p>

<p>The five artists awarded with the Migros Award for the Culture, during the "Che c'è di nuovo? La scena artistica emergente in Ticino" exhibition, (What's New? The emergent artistic situation in Ticino), are coming back with "Non-linear Correlations", an event promoted by the Cultural Institute La Fabbrica, Losone (TI).</p>

<p>These five artists, different both for their styles and for their techniques, were all born between the end of the Sixties and the beginning of the Seventies; they are all rooted to the same cultural background, but their creative poetics is personal and  unique.</p>

<p>Katia Bassanini offers a personal insight into the world, made of competition and supremacy desire, through her keen-witted irony. Hers is a poetics of the absurd which suggests deep riflections. Gian Paolo Minelli shows a set of photographs (pictures) taken in the Carcel de Caseiros: it is a social report springing from the traces the prisoners left behind. The same social elements can be found in Andrea Crociani, whose writings propose a meditation on anarchy; through 36 slides, he also "researches" into violence as an element spreading throughout the world. The duet Terzaghi/Zurcher investigates, on the contrary, into a possible new language forntier, made of coherent juxstapositions of regenerated elements.<br />
 <br />
allegati/attachment</p>

