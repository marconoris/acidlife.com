<div class="tw_title">iXem</div>
<div class="tw_comm"><div class="legg">"Ciao, questa mail informa su 3 importanti iniziative promosse da iXem (italian eXperimantal electronic media [www.ixem.it]) e che riguardano tutti coloro che si occupano di arte e sperimentazione elettronica in italia. Questi gli argomenti..."</div></div>
<div class="legg"><b>1. Live!iXem 2004: </b>concorso nazionale di musica e arti elettroniche
<b>2. ElectricGarden:</b> Mappatura delle realtà  italiane che si muovono nell¹ambito delle arti sperimentali elettroniche
<b>3. ixemnews: </b>creazione di una mainglist sugli eventi e le informazioni importanti che riguardano il panorama della sperimentazione elettronica in Italia.

<p><br />
<b>1. Live!iXem 2004</b></p>

<p>Tornando all'oggetto principale, ricordiamo che tra circa 2 settimane si chiudono le iscrizioni al contest Live!iXem 2004.<br />
Tuttavia dal momento che abbiamo avuto richieste da parte di artisti/gruppi che sono in ritardo nel preparare il materiale si è deciso di accettare una pre-iscrizione via email entro la deadline originaria.<br />
A coloro che avrano effettuato la pre-iscrizione entro il 15/5/2004 sarà possibile consegnare per posta il materiale per l'iscrizione fino al 30/05/2004.<br />
Per effettuare la preiscrizione è sufficiente inviare all'indirizzo email info@ixem.it la scheda di iscrizione allegata al bando (http://www.ixem.it/bando_Live!iXem_2004.pdf) debitamente compilata.</p>

<p>Se chi riceve questa informazione non è direttamente interessato in quanto non artista/performer è cortesemente pregato di inoltrarla a coloro che potrebbero esservi interessati.</p>

<p>Per chi invece ne riceve notizia solo si ricorda che  Live!iXem 2004 è un concorso nazionale di musica e arti elettroniche rivolto a tutti coloro che operano creativamente e sperimentalmente avvalendosi di suoni e immagini (da e su qualunque supporto) al fine di realizzare performances dal vivo. L'iscrizione e la partecipazione al concorso sono gratuite.<br />
Tale iniziativa, volta a fornire una panoramica sullo stato attuale della musica e delle arti elettroniche sperimentali in Italia è organizzata secondo i meccanismi di un contest, si concretizza nella realizzazione di un DVD catalogo che rappresenterà un documento di consultazione audiovisivo, permanente, informativo e divulgativo dei lavori e degli artisti selezionati e partecipanti al concorso.<br />
I sei artisti/gruppi vincitori del concorso saranno gli stessi che, in previsione della realizzazione di una rassegna dal vivo intitolata Live! iXem 2004, verranno invitati, dietro compenso, a presentare i loro lavori e ad esibirsi dal vivo.<br />
Il bando del concorso e dettagliate indicazioni sono reperibili all'indirizzo:  http://www.ixem.it/index.php?sezione=liveixem<br />
Ulteriori informazioni e comunicazioni: info@ixem.it</p>

<p><b>2. ElectricGarden</b></p>

<p>iXem rappresenta un network di artisti e musicisti italiani interessati alla sperimentazione nelle arti e nella musica contemporanea.  Coloro che attualmente si muovono attivamente per iXem sono solo una parte di una realtà ben più vasta che intendiamo portare alla luce con questa iniziativa. Lo scopo di ElectricGarden non è solo quello di realizzare un censimento nazionale, ma è anche quello di offrire una piattaforma di scambio e di incontro per chi sul territorio nazionale si occupa di sperimentazione elettronica.<br />
Chiunque ritenga che il proprio lavoro abbia un approccio sperimentale può recarsi sul sito www.ixem.it  cliccare sulla voce mappatura e compilare il form per segnalarci la sua attività.<br />
Il nostro coordinatore raccoglierà le vostre segnalazioni e provvederà ad inserirle in un archivio che verrà pubblicato quanto prima.</p>

<p><b>3. ixemnews</b></p>

<p>Iscriversi alla mailing list di iXem, è utile a coloro che pur non partecipando fattivamente alle attività di iXem (peraltro aperte a tutti: www.ixem.it) attraverso il forum (http://it.groups.yahoo.com/group/iXem/) sono interessati a venire a conoscenza di eventi speciali e informazioni fondamentali inerenti all'ambito della sperimentazione elettronica in Italia e distribuite in modo essenziale.</p>

<p>Si ricorda che questa mail è un invito ad iscriversi alla mailing list, ricevendo questa mail NON siete automaticamente iscritti.<br />
Avete ricevuto questa mail per uno dei seguenti motivi:</p>

<p>- collaborate o avete collaborato con iXem<br />
- collaborate o avete collaborato con aderenti ad iXem<br />
- avete mandato materiale informativo/promozionale ad iXem<br />
- vi occupate direttamente o indirettamente di arte sperimentale o in particolare di sperimentazione elettronica</p>

<p>Per ricevere le news è sufficiente inviare una mail dall'indirizzo di posta col quale si intende iscriversi a questo indirizzo: ixemnews-subscribe@ixem.it (A breve sarà anche possibile iscriversi dalla home page del sito www.ixem.it)</p>

<p>La mailing list è moderata e prevede soltanto l'invio di poche e essenziali news gli iscritti ai quali è quindi impossibile diffondere a loro volta messaggli alla lista.</p>

<p>Gli iscritti possono contattare il proprietario della lista: ixemnews-owner@ixem.it.</p>

<p>Gli iscritti possono avere informazioni sul funzionamento della lista: ixemnews-help@ixem.it</p>

<p>Gli iscritti possono annullare la propria iscrizione alla lista: ixemnews-subscribe@ixem.it</p>

<p><br />
Sperando che abbiate gradito essere informati sulle nostre iniziative vi inviamo i nostri saluti.</p>

