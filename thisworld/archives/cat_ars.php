<p>Ce fut pendant la deuxième moitié du XIXe siècle que les Juifs de la péninsule arabique méridionale, victimes de plusieurs pogroms et réduits à la condition de dhimmi, c'est-à-dire de citoyens de rang inférieur, pour la plupart provenant de Aden, protectorat britannique depuis 1839, émigrèrent vers les côtes occidentales de la Mer Rouge, et surtout vers Massawa, séduits par les perspectives d'urbanisation et de développement commercial que la colonie italienne de fraîche date semblait promettre.</p>

<p>D'autres Juifs, provenant du bassin de la Méditerranée, favorisés par l'ouverture récente du Canal de Suez, s'installèrent au début du XXe siècle à Asmara, devenue capitale de l'érythrée italienne.</p>

<p>Beaucoup d'entre eux avaient des racines italiennes, parfois très anciennes. Il leur paraissait naturel de pouvoir parler de nouveau la langue que leurs ancêtres chassés d'Espagne avaient dû apprendre en Italie.</p>

<p>Le tremblement de terre de 1921 poussa vers le haut-plateau ceux qui étaient restés à Massawa. La communauté juive se consolida autour de la synagogue d'Asmara, même si ses activités se développaient partout dans la colonie.</p>

<p>La communauté s'agrandit tellement qu'en 1941 les Anglais, au moment de leur arrivée en érythrée en comptèrent à peu près 500, une présence importante, si on la compare au nombre d'habitants du pays. De la même façon pendant le fascisme, des Juifs furent accueillis en érythrée, où les lois raciales étaient appliquées de manière moins dure qu'en Italie.</p>

<p>Si l'alyiah (= la loi du retour à la terre promise) en diminua le nombre, le régime du dictateur éthiopien Menghistu fit ensuite le reste. En 1975 la communauté était réduite à très peu de personnes.</p>

<p>Aujourd'hui on trouve des traces bien évidentes de leur histoire entre Asmara et Massawa, où la mémoire de la présence juive est conservée avec nostalgie par les plus vieux et les fidèles des autres religions.<br />
</p>


