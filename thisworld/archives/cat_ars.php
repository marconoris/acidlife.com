<div class="legg">Domenico Olivero ha concepito un’operazione ambientale di chiaro significato spirituale, che intende coinvolgere i visitatori in una sorta di percorso esistenziale di purificazione individuale e collettiva. L’intervento dell’artista dà vita ad una suggestiva e personale rappresentazione di un’antica forma popolare del culto religioso, densa di mistero e di fascino 
