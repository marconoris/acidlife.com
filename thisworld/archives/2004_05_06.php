<div class="tw_title"><a name="000021">Forum Sociale Antimafia 2004</a></div>
<div class="tw_comm">
<div class="legg">
Per il terzo anno consecutivo a Cinisi, nei giorni 7-8-9 maggio si terrà il Forum Sociale Antimafia, un incontro in memoria di Peppino Impastato e della sua lotta antimafia, che è divenuto dalle un appuntamento per tutte le realtà siciliane e non che si impegnano per la lotta alla mafia nelle sue molteplici forme, dall'eco mafia che devasta il territorio ad una mafia più "istituzionale" che vede protagonisti nomi illustri del panorama politico siciliano. Anche quest'anno come per le scorse edizioni del Forum [ 1 - 2 ], Indymedia Sicilia sarà presente per tutta la durata della tre giorni per garantire una copertura totale agli eventi del Forum Antimafia...</div>

<div class="legg">
::Il calendario della tre giorni::

<p><a href="http://www.centroimpastato.it/" target="new">http://www.centroimpastato.it/</a><br />
<a href="http://italy.indymedia.org/" target="new">http://italy.indymedia.org/</a></p>

<p><br />
1978-2004 Mafia e antimafia a 26 anni dall'assassinio di Peppino Impastato - Terza mobilitazione nazionale antimafia a Cinisi, 7-8-9 maggio 2004</p>

<p>::Programma del Forum Sociale Antimafia 2004::</p>

<p>giovedì 6 maggio:<br />
ore 19:00 > inaugurazione della mostra fotografica "ribellarsi è giusto" di Gabriella Ebano, "Peppino Impastato e dintorni" di Pino Manzella. Davanti casa Impastato - c.so umberto,220</p>

<p>venerdì 7 maggio:<br />
ore 09:00 - animazione di strada con i bambini delle scuole elementari<br />
ore 16:00 - Forum "la memoria rivisitata:resistenza antimafia e resistenza antifascista tra emergenza e revisionismo"<br />
ore 21:00 - "L'ultima violenza" di Giuseppe Fava, a cura dell'associazione "Giuseppe Fava"<br />
ore 23:00 - Concerto in piazza a Cinisi</p>

<p>sabato 8 maggio:<br />
ore 09:00 - animazione in strada con i bambini della scuola elementare di Cinisi<br />
ore 10:00 - Forum "uso sociale dei beni confiscati ai mafiosi. Esperienze e problemi"<br />
ore 15:00 - Forum "mafia, precarietà, salario e reddito sociale><br />
ore 17:00 - Forum "Il saccheggio del territorio. dalla terza pista al ponte sullo stretto"<br />
ore 21:00 - Spettacolo di musica popolare in piazza a Cinisi</p>

<p>domenica 9 maggio:<br />
ore 10:00 - Forum "Mafie e potere nel villaggio globale.questione morale, crisi della democrazia e nuovo protagonismo"<br />
ore 16:00 - Corteo dalla sede di Radio Aut di Terrasini a Cinisi<br />
ore 21:00 - Concerto in piazza a Cinisi</p>

