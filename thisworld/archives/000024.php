<div class="tw_title">Amnesty International's 12-point Program for the Prevention of Torture by Agents of the State</div>
<div class="tw_comm">


<p><br />
<div class="legg"><br />
<b>1.	Condemn torture</b></p>

<p>The highest authorities should condemn torture in all its forms whenever it occurs. They must make clear to all members of the security forces and judiciary that torture will never be tolerated.<br />
The leaders of armed political groups must also make clear to their forces that torture is always unacceptable.</p>

<p><b>2.	Ensure access to prisoners</b></p>

<p>Torture often takes place while the victims are held incommunicado -- unable to contact people outside who could help them or find out what is happening to them. The practice of incommunicado detention should be ended. Governments should ensure that all prisoners are brought before an independent judicial authority without delay after being taken into custody. Prisoners should have access to relatives, lawyers and doctors without delay and regularly thereafter.</p>

<p><b>3.	No secret detention</b></p>

<p>In some countries torture takes place in secret locations, often after the victims are made to "disappear". Governments should ensure that prisoners are held only in officially recognized places of detention and that accurate information about their arrest and whereabouts is made available immediately to relatives, lawyers and the courts. Effective judicial remedies should be available at all<br />
times to enable relatives and lawyers to find out immediately where a prisoner is held and under what authority and to ensure the prisoner's safety.</p>

<p><b>4.	Provide safeguards during interrogation and custody</b></p>

<p>All prisoners should be immediately informed of their rights. These include the right to lodge complaints about their treatment and to have a judge rule without delay on the lawfulness of their detention. Judges should investigate any evidence of torture and order release if the detention is unlawful. A lawyer should be present during interrogations. Governments should ensure that conditions of detention conform to international standards for the treatment of<br />
prisoners and take into account the needs of members of particular vulnerable groups. The authorities responsible for detention should be separate from those in charge of interrogation. There should be regular, independent, unannounced and unrestricted visits of inspection to all places of detention.</p>

<p><b>5.	Prohibit torture in law</b></p>

<p>Governments should adopt laws for the prohibition and prevention of torture incorporating the main elements of the UN Convention against Torture and Other Cruel, Inhuman or Degrading Treatment or Punishment (Convention against Torture) and other relevant international standards. All judicial and administrative corporal punishments should be abolished. The prohibition of torture and the essential safeguards for its prevention must not be suspended under any circumstances, including states of war or other public emergency.</p>

<p><b>6.	Investigate</b></p>

<p>All complaints and reports of torture should be promptly, impartially and effectively investigated by a body independent of the alleged perpetrators. The methods and findings of such investigations should be made public. Officials suspected of committing torture should be suspended from active duty during the investigation. Complainants, witnesses and others at risk should be protected from intimidation and reprisals.</p>

<p><b>7.	Prosecute</b></p>

<p>Those responsible for torture must be brought to justice. This principle should apply wherever alleged torturers happen to be, whatever their nationality or position, regardless of where the crime was committed and the nationality of the victims, and no matter how much time has elapsed since the commission of the crime. Governments must exercise universal jurisdiction over alleged torturers or extradite them, and cooperate with each other in such criminal<br />
proceedings. Trials must be fair. An order from a superior officer must never be accepted as a justification for torture.</p>

<p><b>8.	No use of statements extracted under torture.</b></p>

<p>Governments should ensure that confessions or other evidence obtained through torture may never be invoked in legal proceedings, except against a person accused of torture.</p>

<p><b>9.	Provide effective training</b></p>

<p>It should be made clear during the training of all officials involved in the custody, interrogation or medical care of prisoners that torture is a criminal act. They should be instructed that they are obliged to refuse to obey any order to torture.</p>

<p><b>10.	Provide reparation</b></p>

<p>Victims of torture and their dependants should be entitled to obtain prompt reparation from the state including restitution, fair and adequate financial compensation and appropriate medical care and rehabilitation.</p>

<p><b>11.	Ratify international treaties</b></p>

<p>All governments should ratify international instruments containing safeguards and remedies against torture, including the UN Convention against Torture with declarations providing for individual and inter-state complaints.</p>

<p><b>12.	Exercise international responsibility</b></p>

<p>Governments should use all available channels to intercede with governments where torture is reported. They should ensure that transfers of training and equipment for military, security or police use do not facilitate torture. Governments must not forcibly return a person to a country where he or she risks being tortured.</div></p></div>
