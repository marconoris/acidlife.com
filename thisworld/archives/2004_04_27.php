<div class="tw_title"><a name="000008">Monjoie</a></div>
<div class="tw_comm">
<p>La Fondazione Peano è lieta d’invitarla alla mostra<br />
"Monjoie", installazione scultorea di Domenico Olivero.</p>

<p>Inaugurazione Sabato 8 Maggio dalle ore 18,00 alle ore 20,00</p>

<div class="legg">Domenico Olivero ha concepito un’operazione ambientale di chiaro significato spirituale, che intende coinvolgere i visitatori in una sorta di percorso esistenziale di purificazione individuale e collettiva. L’intervento dell’artista dà vita ad una suggestiva e personale rappresentazione di un’antica forma popolare del culto religioso, densa di mistero e di fascino 
(E.Perotto, curatore della Fondazione Peano)

<p><br />
   Questo progetto prende spunto dalla tradizione alpina delle Monjoie, piccole piramidi di sassi che sono create durante i pellegrinaggi verso i santuari mariani presenti sui monti alpini, sia per segnalarlo sia come forma votiva. L’installazione è articolata in due parti; una nel parco esterno, costituita dalle sculture che s'ispirano ai sette peccati capitali, una nello spazio interno, costituita da una grande installazione dedicata alle virtù teologali. Tutto il progetto è stato pensato appositamente per questa particolare occasione espositiva. In questa tappa l'artista affronta nel suo linguaggio personale il rapporto con la fede e con i propri bisogni spirituali.</p>

<p><br />
   Durata della mostra dal 11 al 15 Maggio 2004,<br />
Orario d'apertura dal Martedì al Sabato, orario 16,00 alle 19,00.  Ingresso libero.</p>

<p><br />
   Fondazione Peano Corso Francia 47 12100 Cuneo (Italia) <br />
tel: +39.0171.603649 cell. +39 328 21.595.21 @: art_resonance@hotmail.com</p>

<p><br />
   Foto: http://art.supereva.it/artresonance/<br />
             www.fondazionePeano.org</p>

<p></p>

<p>Codesta mostra fa parte di un progetto che per tutto il 2004 si svolgerà sul territorio cuneese. Un attraversamento meditativo dell'artista sull'essere in un determinato spazio, contaminato da una particolare tradizione culturale, come in questo caso quella della provincia subalpina. Queste opere si realizzeranno rielaborando elementi sociali e familiari vissuti direttamente dall'artista, alla scoperta delle ipotesi d'origine. Il nuovo progetto prende il nome globale di "Resonance - l'arte dell'attenzione".</p>

<p>28/1-20/2/04	"1.9.6.4_7" azione visiva presso lo spazio Albume di Cuneo.<br />
