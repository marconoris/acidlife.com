<div class="tw_title"><a name="000027">Coniglioviola alla Fiera del Libro e al MiArt</a></div>
<div class="tw_comm">


<p>CONIGLIOVIOLA NEWSLETTER<br />
www.coniglioviola.com</p>

<p><br />
Lunedì 10 maggio, alla Fiera del Libro di Torino,  nello Spazio della Città di Torino<br />
Carlo Infante presenta il libro di cui è autore<br />
"Performing Media<br />
la nuova spettacolarità della comunicazione interattiva e mobile"<br />
edito da Novecento::libri </p>

<p>intervengono: <br />
-  Fiorenzo Alfieri, Assessore alle Risorse e allo Sviluppo della Cultura della Città di Torino<br />
- Paolo Messina, dirigente del Settore Sistema bibliotecario urbano di Torino<br />
- Coniglioviola<br />
- gli autori multimediali della rassegna Creatività Digitale<br />
- gli autori teatrali di Laundrette Soap</p>

<p>Coniglioviola interverrà in merito al nuovo progetto The Violet Globe / Net.dramas di cui si parla all'interno del libro.</p>

<p>info: www.performingmedia.org</p>

<p>**************************************************</p>

<p>Venerdì 14 maggio, ore 16.00, al MiArt (Milano)<br />
Sala Provincia di Milano – padiglione 3</p>

<p>"Le forme del digitale" <br />
incontro-performance su arte, nuove avanguardie e tecnologie, a cura di Angelo Capasso. </p>

<p>Intervengono: Coniglioviola, Cristina Chiappini, EpidemiC, Franco Zeri, Limiteazero, Mino Freda, Marta Dell'Angelo, Stefano Cagol, Zerynthia Radioartemobile. </p>

<p>info: www.miart.it</p>

