      In caso di lacrimogeni avere le lenti a contatto è peggio. <br>
      I gas restano intrappolati sotto la lente e possono provocare lesioni corneali, 
      oltre ad una forte irritazione. <br>
      Bisogna levarle immediatamente, ma con le mani pulite, altrimenti si peggiora 
      la situazione. Meglio portare gli occhiali da vista, e lasciare le lenti 
      nello zaino (sperando che non siano considerate "oggetti atti ad offendere"). 
      Agli occhialini da piscina va mantenuta intatta la protezione antinebbia, 
      qualunque cosa essa sia. Vanno bene anche gli occhiali da saldatore e quelli 
