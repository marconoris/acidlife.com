 <p> - INDICAZIONI RELATIVE AI GAS LACRIMOGENI<br>
  In caso di esposizione :<br>
  - Mantenere la calma<br>
  - Non toccare o toccarsi la faccia o fregare gli occhi<br>
  - Cercare un ambiente areato<br>
  - Tenere gli occhi aperti<br>
  - Respirare lentamente e profondamente<br>
  - Soffiare il naso e sputare<br>
  - Lavarsi con abbondante acqua a temperatura ambiente la faccia gli occhi e 
  le mani (no acqua calda)<br>
  - Fare numerosi gargarismi (sputare l'acqua)<br>
  - Appena possibile lavare tutto il corpo con acqua a temperatura ambiente (no 
  acqua calda)<br>
  - Sostituire i vestiti e riporli in un sacco di plastica e chiuderlo<br>
  - Evitare di entrare in un ambiente chiuso con gli indumenti contaminati<br>
