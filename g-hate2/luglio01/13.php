  - ATTAC Perquisita -<br>
  Questa mattina all'alba uomini della Digos di Taranto hanno perquisito in forza 
  l'abitazione dei familiari di un rappresentante di ATTAC Italia nonché 
  uno dei sedici portavoce del Genoa social forum, Fiorino Iantorno, da mesi impegnato 
  all'interno del GSF per la costruzione delle mobilitazioni di luglio contro 
  il vertice dei G8 . "Art 41 delle leggi antiterrorismo" è stata 
  la giustificazione, ovvero una provvidenziale segnalazione anonima sulla presenza 
  di armi ed esplosivi. Inutile dire che la perquisizione non ha portato ha nessun 
  risultato. <br>
  Come Genoa Social Forum denunciamo questo gravissimo atto di intimidazione, 
  che nulla può avere a che fare con presupposte esigenze di sicurezza, 
  ma che rientra invece nel clima di paura e di tensione che si vuole creare rispetto 
  alle mobilitazioni della prossima settimana a Genova. <br>
  Il Gsf ritiene responsabile di questo e tutti gli atti di intimidazione che 
  si ripetono in questi giorni, direttamente il Ministero degli Interni che alimenta 
  un pericoloso clima di intimidazione nei confronti di tutti coloro che stanno 
  lavorando per la costruzione di una settimana pacifica e non violenta. </p>
