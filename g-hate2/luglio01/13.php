     <p>- Firenze contro il G8<br>
  Un immigrato haitiano torturato da alcuni agenti di polizia con un bastone nel 
  bagno di un commissariato di Brooklyn, ha ottenuto un risarcimento di 8,7 milioni di dollari,pari a circa 20 miliardi di lire. <br>
  L'uomo, Abner Louina, si èdetto fortunato di aver avuto giustizia e ha 
  espresso il desiderio di adoperare una parte della somma per creare una fondazione 
  a favore delle vittime delle violenze della polizia.<br>
  La vicenda aveva fortemente compromesso i rapporti tra la comunita' di colore 
  e l'amministrazione della citta' di New York.<br>
