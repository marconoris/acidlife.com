          <div class="tit"><b>SABATO 18 AGOSTO 2001</b></div>
          <br><div class="ver12">
          - G8. Verdi: consegneremo dossier a Corte europea diritti umani ''Porteremo 
          alla Corte europea dei diritti umani un dossier contenente tutte le 
          testimonianze di coloro che hanno subito violenza durante il vertice 
          del G8''. <br>
          Lo annunciano Loredana De Petris e Francesco Martone, senatori dei Verdi. 
          ''Noi parlamentari verdi - dicono - eravamo a Genova, abbiamo assistito 
          a una vera e propria mattanza e abbiamo raccolto numerose testimonianze. 
          <br>
          La caserma di Bolzaneto si e' trasformata in un centro degli orrori, 
          dove tutti i ragazzi fermati sono stati sottoposti a vere e proprie 
          torture fisiche e psicologiche. Risulta addirittura che i ragazzi siano 
          stati costretti a cantare 'giovinezza' e a gridare 'viva il duce e la 
          polizia'. <br>
          E' una storia incredibile - aggiungono - e non possiamo permettere che 
          certe vicende vengano nascoste ai cittadini. Sono vicende che la nostra 
          storia ha gia' visto: un evento di mussoliniana memoria''. <br>
          <b><a href="http://www.verdi.it%20" target="_blank">www.verdi.it </a></b> 
          <p>- G8: Verdi, nelle carceri le prove di violenza <br>
            Il senatore dei verdi Fiorello Cortiana ha visitato ieri il carcere 
            femminile di Voghera e quello maschile di Pavia, dove sono reclusi 
            alcuni cittadini stranieri fermati durante le manifestazioni di Genova. 
            ''Stando alla ricostruzione degli eventi - ha raccontato Cortiana 
            - pare che la clinica del carcere di Voghera si sia trovata a dover 
            operare nei confronti dei detenuti provenienti da Genova, che si sono 
            presentati con traumi e arti rotti''. <br>
            A Voghera sono ancora detenute sette cittadine austriache, una slovena, 
            un'inglese ed un'americana; a Pavia ci sono quattro tedeschi e un 
            irlandese. ''I quattro tedeschi - ha detto il senatore verde - sono 
            stati costretti a firmare i verbali sotto la minaccia di essere picchiati. 
            <br>
            Questi eventi gettano un'ombra grave su quello che dovrebbe essere 
            uno stato di diritto''. I Verdi, ha detto Cortiana, hanno deciso di 
            proporre l'istituzione di una commissione internazionale sui fatti 
            di Genova.<br>
