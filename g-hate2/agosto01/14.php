          - &quot;Dalla scuola gli attacchi degli hacker&quot;<br>
          Si indaga sui &quot;pc&quot; della Diaz il blitz<br>
          GENOVA - La Procura di Genova ha aperto un fascicolo giudiziario sull'aggressione 
          di un gruppo di &quot;hacker&quot; antiG8 ai danni degli archivi telematici 
          del Ministero dell'Interno. <br>
          L'inchiesta numero 7 sul vertice internazionale e` anch'essa collegata 
          al blitz della scuola elementare Diaz, perche` il &quot;netstrike&quot; 
          - cosi` si chiama in gergo questo tipo di attacco in rete, che punta 
          ad &quot;occupare&quot; un sito web con il maggior numero di contatti 
          possibile, fino a renderlo inutilizzabile - potrebbe essere partito 
          dai terminali che si trovavano all'interno dei locali di via Cesare 
          Battisti. <br>
          Al termine degli arresti, nell'istituto scolastico gli investigatori 
          hanno sequestrato quaranta computer, centinaia di dischetti e diversi 
          altri supporti informatici che sono stati consegnati agli esperti della 
          polizia postale. Si tratta di materiale che ufficialmente era stato 
          utilizzato da persone legate a diverso titolo al Genoa Social Forum 
          (attivisti, giornalisti, legali) ma che era accessibile anche ad altri 
          frequentatori della Diaz. L'attacco degli &quot;hackers&quot; era fallito 
          perche` il Ministero, avvertito da un'informativa dei servizi, aveva 
          provveduto in tempo ad allargare i propri provider.<br>
