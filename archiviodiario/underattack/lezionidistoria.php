&Egrave; iniziata la scuola in molte citt&agrave; d'Italia. 
Potremmo fare una lezione di storia per ragionare 
lucidamente su questa guerra imminente.<br><br>

Come docente della materia inviterei i colleghi e gli 
studenti a riflettere su come nascono le guerre e a 
compiere una ricerca per vedere quante guerre sono iniziate 
con le stesse dinamiche con cui sta nascendo la guerra 
attuale, per ora solo dichiarata sui giornali. <br>
Io ho cercato di farlo nelle mie classi. L'ho fatto 
partendo da alcune preziose informazioni che ho trovato sui 
libri di storia.<br><br>

Spesso le guerre scoppiano nello stesso modo: un indizio 
diviene una prova e un atto di terrorismo viene scambiato 
per un atto di guerra.<br>
Ma nonostante si vada a scuola non si impara mai abbastanza 
e non si sfogliano neppure i libri quando l'Impero ci 
chiama a combattere. E di Impero vorrei parlare, quello 
dell'Austria-Ungheria di 87 anni fa.<br><br>

In questi giorni l'Afghanistan &egrave; accusato di coprire il 
terrorismo e si d&agrave; per certo ci&ograve; che si deve dimostrare: la 
regia di quello Stato dietro l'attentato. <br>
Vi invito a leggere questo testo qui sotto in cui si 
analizza come una superpotenza dell'inizio del secolo 
scorso (l'Impero austroungarico) dette per certa la 
copertura della Serbia per l'attentato terroristico in cui 
per&igrave; l'erede al trono. <br>
&Egrave; la storia di come &egrave; scoppiata la prima guerra mondiale, 
guerra scaturita da un atto di terrorismo che venne 
considerato atto di guerra. <br>
&Egrave; la storia di indizi che dovevano risultare certezze. 
Alla fine gli indizi si persero per strada e rimasero solo 
le cannonate. <br>
Evidenzio in grassetto [QUI IN MAIUSCOLO] le parti che mi 
sembrano piu' interessanti.<br><br>

