Ho cercato varie definizioni di guerra e tutte danno torto 
a Bush che tenta di convincere noi e il mondo che l'orrenda 
serie di attentati negli Usa siano un "atto di guerra". Una 
guerra pu&ograve; causare meno morti di un atto di terrorismo, non 
&egrave; nella enorme e mostruosa lista dei morti americani di 
questi giorni che troviamo la ragione per dire "guerra". <br>
Ecco come &egrave; definita la "guerra" sui libri che usiamo a 
casa e a scuola.<br><br>

"Lotta armata tra due popoli o fra due o pi&ugrave; Stati divisi 
in campi opposti". <br>
Enciclopedia Generale De Agostini Compact. Edizione 1988 <br><br>

"Contesa armata tra due o pi&ugrave; Stati". <br>
Pittano Giuseppe, Bidizionario italiano linguistico e 
grammaticale. Edizione 1981 <br><br>

"La lotta armata tra due o pi&ugrave; Stati o tra fazioni di uno 
stesso Stato". <br>
Dizionario Garzanti della lingua italiana. Edizione 1980 <br><br>

"Lotta tra due stati o all'interno di uno stato, condotta 
con le armi, con o senza l'osservanza del diritto 
internazionale in materia". <br>
Sabatini Francesco, Coletti Vittorio, DISC Dizionario 
Italiano Sabatini Coletti. Edizione 1997 <br><br>

Sorge a questo punto il dubbio se siano ancora validi i 
dizionari e se noi insegnanti serviamo ancora a qualcosa. E 
se la cultura e la guerra possano convivere senza che l'una 
elimini l'altra. <br>
La guerra del Golfo fu definita "operazione di polizia 
internazionale" per non confliggere con l'articolo 11 della 
Costituzione Italiana. La guerra in Kossovo venne definita 
"operazione umanitaria" per la stessa ragione. Vennero 
cambiate le parole perch&eacute; non potevano cambiare le leggi. 
Ora che la parola guerra dovrebbe essere abolita dai 
giornalisti e dai politici in quanto non corrispondente al 
significato codificato nella lingua dai discorsi (di 
Terrorismo e non di guerra si tratta e le definizioni qui 
sopra sono limpide) ecco che invece salta fuori la 
parolaccia: guerra. La ragione? Un terrorista si deve 
arrestare e processare, un nemico lo si pu&ograve; ammazzare senza 
bisogno di processo. <br>
Di guerra parl&ograve; il Movimento Sociale Italiano di Giorgio 
Almirante al tempo delle Brigate Rosse, chiedendo l'uso 
dell'esercito e invocando la pena di morte, tanto per 
intenderci. Ma in quel caso Almirante aveva una ragione in 
pi&ugrave; di Bush: considerava le BR come promotrici di una 
guerra civile. E la "guerra" aveva una sua pretestuosit&agrave; 
semantica nella retorica di Almirante. <br>
Ma Bush ci chiede di metter da parte i libri, la cultura, 
il significato delle parole e ci dice semplicemente: nulla 
sar&agrave; pi&ugrave; come prima.<br><br>

