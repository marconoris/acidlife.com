<div class="note">July 23, 2038</div><div class="title">Potere e Umanit&agrave;.</div><br><div class="ver">
"Erano 37 anni che gli incontri dei *Grandi della Terra* non venivano organizzati in luoghi 
pubblici. Il tentativo di rilanciare l'immagine dei rappresentanti dei 
paesi pi&ugrave; potenti del Mondo non &egrave; riuscita.<br>
Nei giorni degli incontri centinia di miglia di donne e uomini si sono 
presentati spontaneamente a Genova per ricordare a tutto il Mondo che in 37 anni non solo non sono stati risolti i problemi di inquinamento, fame, malattie, 
ma che tutto questo &egrave; stato aggravato.<br>
Un'assemblea spontanea formatasi nel centro della citt&agrave; ha permesso di 
rendere nota alla stampa le richieste dei manifestanti.<br><br>
I pi&ugrave; anziani ricorderanno la grande manifestazione dei giorni del luglio 
del 2001 quando 150.000 persone si recarono nella citt&agrave; ligure per 
manifestare il loro dissenso contro quelli che all'epoca erano ancora 8 Grandi.
Come dimenticare il giovane di 20 anni ucciso da un carabiniere da pochi 
metri, a sangue freddo? E come tacere della repressione che in tutti quei 
giorni le forze dell'ordine condussero contro tutti coloro che cercavano di 
garantire informazione libera? Giornalisti e semplici manifestanti furono 
fermati, picchiati, le loro attrezzature distrutte. Violata ogni minima 
garanzia della allora Costituzione italiana nata dalla Resistenza della II 
Guerra Mondiale.<br>
Finite le manifestazioni ufficiali, le forze dell'ordine iniziarono azione 
di rappresaglia violando i diritti garantiti anche dalle organizzazioni 
mondiali governative.<br>
Si parl&ograve; da parte degli storici di deriva autoritaria e reazionaria come 
spesso in Italia era accaduto per affrontare il dissenso e la lotta sociale.
Gli storici ricordano le lotte operaie degli anni 50 nel dopo guerra 
italiano, gli anni delle proteste studentesche del 1968 e 1977.
Ma dopo quasi 30 anni le forze democratiche del paese pensavano che oltre 
non si potesse andare e invece non fu cos&igrave;.<br>
Ma tutto questo non sar&agrave; mai raccontato nelle scuole, tutto questo rimane 
nella Rete a memoria dell'eterno conflitto tra Potere e Umanit&agrave;."<br><br>


<i>Dall'introduzione al saggio "<b>Rinascita della lotta sociale mondiale</b>" a cura 
