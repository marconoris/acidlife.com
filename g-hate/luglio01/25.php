  "[...] La partita giocatasi in quel di Genova fra Black Block e Block 
  Busters (Ps, CC, Tutine Bianche, ecc.) è stata un successo di audience. 
  <br>
  E' ovvio, perchè questo avviene in ogni soap opera quando è facile 
  distinguere nettamente i buoni dai cattivi.<br>
  C'è però un problema: io non credo ai buoni e ai cattivi.<br>
  Detto ciò, è un fatto che a Genova la violenza ci sia stata, da 
  una parte come dall'altra. Da ambo i lati, la propria violenza viene giustificata. 
  Ma esiste una violenza buona ed una cattiva? Si, no, forse. Date le premesse, 
  penserete che voglia prendere le distanze dalla gratuita violenza dei nero-vestiti? 
  Si e No. Forse. Si, ma soprattutto<br>
  No!. Io non credo ai buoni e ai cattivi; non ci credevo nemmeno quando da piccolo 
  li vedevo chiaramente vergati sulla lavagna in due angoli diversi. Poi i buoni 
  mi stanno istintivamente e cordialmente sul glande, soprattutto perchè 
  in genere sono ipocriti e sempre pronti a fare gli infamoni con la signora maestra. 
  Mi sembra che nel "movimento" , nel "popolo di Seattle" 
  (che cazzata!) in versione ligure l'omino buonino abbia fatto la parte della 
  maestrina e omino bianco quello del capo-classe, pronto a richiamare maschiamente 
  all'ordine (all'ordine, capite?) gli scalmanati, i "Franti" della 
  situazione, perchè loro possono in tal modo conquistare l'angolo dei 
  buoni; infatti ci vogliono dei cattivi per far vedere che babà di cristiani 
  siamo, "quanto sa di sale l'accendere e scassare le altrui sale, ma quanto 
  può essere dolce l'agnoletto pasquale".[...]"<br>
