            Vengono sempre mille dubbi prima di imbarcarsi in un viaggio come 
            questo. Ti chiedi a cosa serve andare in una citt&agrave; militarizzata, 
            piena di tensione, a manifestare contro una riunione di otto persone 
            che poi alla fine non decideranno niente, perch&eacute; le deliberazioni 
            importanti sono gi&agrave; state prese nei corridoi, sui fili dei 
            telefoni riservati, nelle salette antistanti i parlamenti nazionali. 
            <br>
            Ti domandi che differenze puoi fare tu, che oltre a tutto ti senti 
            molto ignorante sui temi complessissimi e spinosi della globalizzazione, 
            e non vuoi fare di tutta un'erba un fascio - perche' c'e' globalizzazione 
            e globalizazzione.[...]<br>
            [...] IL VIAGGIO<br>
            Partiamo con una macchina che abbiamo affittato, vuoi per la maggior 
            comodit&agrave; negli spostamenti, vuoi per il limpido comportamento 
            dello Stato Italiano relativamente ai treni.<br>
            Viaggio assonnatissimo; si parla poco, io dormo per quasi tutto il<br>
            tempo. Scoprir&ograve; poi il tragitto percorso grazie alle telecamere 
            che<br>
            ci siamo portati, telecamere con le quali hanno ripreso la strada 
            per mettere a punto i colori e l'autofocus.<br>
            Sogni strani, dettati probabilmente dalla tensione. Ma perch&eacute; 
            sono<br>
            cos&igrave; teso? Perch&eacute; devo aver paura di andare ad una manifestazione?[...]<br>
            [...] L'ARRIVO A GENOVA<br>
            Entriamo in una citt&agrave; vuota. Meno spettrale di quanto pensassi, 
            ogni tanto passa qualcuno, ma la sensazione generale &egrave; desolante.<br>
            Incrociamo una pattuglia della celere, che ci fa segno di passare.[...] 
            <br>
            [...] PIAZZA MANIN<br>
            Continuiamo ad allontanarci, vediamo da lontano drappelli di black 
            bloc preceduti dal suono dei loro tamburi da guerra. Se non avessi 
            paura li troverei affascinanti, moderni cavalieri della tavola rotonda 
            votati alla distruzione, con un loro codice, le loro divise, le loro 
            bandiere.<br>
            Ci dirigiamo verso piazza Manin, dove fino a un'ora prima c'era una 
            manifestazione di pacifisti. Seduti per terra, sono stati caricati<br>
            dalla polizia, per fortuna in modo non troppo violento. Incontriamo<br>
            alcune persone contuse, ma nessuna ferita in modo serio.[...]<br>
            [...] GUERRIGLIA URBANA <br>
            A un certo punto la situazione si fa veramente pericolosa, un edificio 
            &egrave; in fiamme davanti a noi, dalla parte di piazzale Kennedy 
            c'&egrave; un oceano di celerini schierati. Ci dividiamo, due di noi 
            hanno un appuntamento in luoghi precisi e non vogliono mancare, cercano 
            di arrivarci per strade laterali, noi altri decidiamo di tornare verso 
            la macchina (o quel che ne rimane) e cercare di raggiungere piazzale 
            Kennedy dai monti. [...]<br>
            [...] LA SERA<br>
            E' calata la sera. Sono molto triste, molto scoraggiato, entro in<br>
            macchina e piango per tre minuti. Un minuto per i black bloc scortati 
            dalla polizia che dovrebbe difendere lo &quot;stato civile&quot;. 
            <br>
            Un minuto per il ragazzo morto e per il ragazzo che l'ha ammazzato. 
            Un minuto perch&eacute; - come dir&agrave; domani una ragazza di Attacc 
            che ha visto cinque lacrimogeni piombare in un colpo solo in un punto 
            assolutamente tranquillo della manifestazione - non capisco il senso 
            di questo gioco a gatto e a topo, e non capisco perch&eacute; noi 
            siamo i topi.[...]<br>
            [...] IL GIORNO DOPO<br>
            Ci svegliamo verso le nove, e scendiamo nella hall preparandoci a<br>
            tornare a Genova. Incontriamo due giornalisti inglesi di Indymedia,<br>
            uno &egrave; stato fermato dai carabinieri perch&eacute; &egrave; 
            arrivato da Genova con una macchina completamente distrutta e due 
            tagli alle gambe.<br>
