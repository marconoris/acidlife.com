Da venerd&igrave; 19 a domenica 21 gennaio si &egrave; svolta al Teatro Polivalente Occupato di Bologna una serie di incontri e dibattiti dal titolo <b>GNU
ECONOMY COPYRIGHT E SOFTWARE LIBERO</b>.<br>Alla manifestazione
organizzata dal <a href="http://www.ecn.org/tpo/">Visual City Virus</a> (gruppo video del TPO),
<a href="http://www.acidlife.com">acidlife.com</a>, <a href="http://www.annozero.org">Annozero.org</a>, <a href="http://dyne.org">Dyne.org</a>, <a href="http://strano.net">Strano Network</a> e gli
hacklab di <a href="mailto:hacklab-bologna-subscribe@egroups.com">Bologna</a>, <a href="http://firenze.hacklab.it/">Firenze</a>, <a href="http://www.freaknet.org">Catania</a>, Torino e <a href="http://www.ecn.org/loa">Milano</a>,
sono intervenuti diversi esperti, programmatori, avvocati,
operatori che si occupano di software libero, lo sviluppano
affermando la libert&agrave; di circolazione dei saperi in Rete.
Molti sono stati gli interventi, le proposte, animatissima
era poi la sala  informatica allestita con incredibile
rapidit&agrave; dai vari "acari" venuti da tutt'Italia a dare il
loro contributo alla discussione e scambiare con gli 
altri saperi e conoscenze sugli sviluppi del software libero
e dare la possibilit&agrave; ai visitatori di vedere praticamente in
funzione, dando vita a veri e proprio workshop improvvisati,
linux o altri software liberi.<br><br>

Ma cos'&egrave; il software libero e la licenza GPL?<br><br>

Una volta che il programmatore ha sviluppato un programma e
ha intenzione di distribuirlo ha di fronte alcune 
strade: pu&ograve; renderlo di pubblico dominio e
quindi perdere completamente la propriet&agrave; intellettuale del
suo lavoro, nonostante questo possa essere copiato e
redistribuito; pu&ograve; decidere di farne un software
proprietario e quindi chiuso, senza rilasciare i sorgenti di
esso, ma solo l'eseguibile; oppure ancora pu&ograve; scegliere la
vita del software libero.<br><br>

La maggior parte del software oggi in circolazione &egrave;
proprietario e immodificabile e la sua diffusione &egrave; gestita
da licenze restrittive che oltre a non fornire all'utente
una piena funzionalit&agrave; del programma non lo proteggono da
eventuali difetti del programma stesso. La licenza Gnu/Gpl 
invece regolamenta la diffusione del software libero,
definito tale perch&eacute; permette all'utente il diritto di copia
del programma e quindi di redistribuzione di esso, il
diritto di accesso ai codici sorgente (quindi il 
diritto di conoscere direttamente le funzioni di un
applicativo), il diritto di modifica dei sorgenti e il
diritto di distribuzione del software 
sviluppato o corretto. L'unica restrizione &egrave; relativa alla
licenza dell'applicativo: se per realizzarlo si utilizzano basi
(sorgenti precedenti, librerie, etc) tutelate dalla
Gnu/Gpl, non si potr&agrave; usare nessun'altra licenza che la Gpl.<br><br>

Ci sono poi altri tipi di licenze che hanno  delle
restrizioni rispetto a quella base. La LGPL &egrave; una licenza
che regola la distribuzione delle librerie e che permette, a
differenza della GPL, la possibilit&agrave; di mantenere
propietario il software derivato da software libero
rilasciato sotto LGPL. La FDL &egrave; la licenza che regola la
distribuzione della documentazione tecnica di supporto al
software libero. In particolare, la FDL permette a tutti la
libert&agrave; di copia e redistribuzione del materiale, con o
senza modifiche, sia a scopo commerciale che 
no-profit. Inoltre consente agli autori e agli editori di
essere riconosciuti per il proprio lavoro pur preservandoli
dalla responsabilit&agrave; di modifiche succesivamente apportate
ai testi.<br><br>

Al di l&agrave; di queste informazioni tecniche che hanno permesso
anche a chi non era troppo informato sull'argomento free
software di partecipare al dibattito, il nodo su cui pi&ugrave; si
&egrave; dibattuto &egrave; stato quello di individuare, sull'obiettivo
comune della diffusione del software libero, una serie di
strategie che permettano ci&ograve;.
Il risultato positivo di questa tre giorni, infatti, &egrave;
delineato proprio un coordinamento composito, in cui sono
rappresentate le diverse realt&agrave; digitali italiane  impegnate
nello sviluppo di una serie di progetti che hanno la
finalit&agrave; di diffondere il pi&ugrave; possibile il software libero e
la sua idea, che ha alla base la libert&agrave; di
diffusione dei saperi.<br><br>

Il primo passo verso questo obiettivo &egrave; quello di rafforzare
la coscienza politica degli informatici in modo 
che sia condivisa la consapevolezza che il free software non
&egrave; soltanto un'alternativa tecnica all'utilizzo di software
proprietario (nel senso di immodificabile), ma una vera e
propria alternativa economica e politica in quanto lo
spirito del free software ruota attorno alla libera
circolazione e manipolazione dei saperi e alla possibilit&agrave;
di un'economia basata non tanto quindi sulla propriet&agrave; del
prodotto ma quanto nel suo sviluppo successivo (a  livello
economico, infatti, gli sviluppatori di free software
guadagnano non tanto dalla vendita del software, ma
dall'assistenza e dallo sviluppo di questi software).<br><br>

In merito a questa crescita della consapevolezza fra gli 
informatici si poneva l'attenzione, quasi alla fine dei
lavori dell'assemblea, sulla questione del reddito di
cittadinanza. Non essendoci nel campo dell'informatica,
magari pi&ugrave; che in altri campi, una netta distinzione tra
lavoro direttamente quantificabile in termini economici e 
tempo impiegato per la formazione e l'apprendimento
indispensabile per il lavoro stesso, esso si trova ad
essere molto vicino al dibattito teorico di alcuni gruppi
politici circa il reddito di cittadinanza ovvero il diritto 
ad un reddito garantito per tutto quel lavoro che le persone
fanno ma che non &egrave; immediatamente quantificabile ma che &egrave;
indispensabile per inserirsi nel mondo del lavoro.<br><br>

Un'altra strategia discussa in merito alla diffusione del
software libero &egrave; la sua diffusione nella pubblica
amministrazione perch&eacute; se cos&igrave; fosse allora si riuscirebbe
veramente a mettere in discussione il monopolio Microsoft e 
la licenza GPL diverrebbe una realt&agrave; concreta al pari degli
altri tipi di licenze. Per fare questo si quindi parlato di
agire su due fronti differenti, da un lato spingere
l'introduzione del free software a livello locale con atti
ed iniziative anche individuali (Beppe Pavoletti e Francesca 
Campora hanno parlato del loro impegno personale e dei
numerosi problemi nell'introduzione di Linux e di altri
software liberi rispettivamente nel mondo bibliotecario e
nella scuola), e dall'altro spingere le istituzioni a 
prendere atto dell'alternativa del software libero puntando
soprattutto sui vantaggi economici che ci&ograve; comporterebbe
(riduzione dei costi e creazione di nuovi posti di lavoro
in assistenza e sviluppo). Quest'ultimo passaggio 
dovrebbe essere affrontato, secondo l'assemblea degli
hacker, da quelle comunit&agrave;, come l'Associazione Software
Libero, che in un certo senso sono pi&ugrave; istituzionalizzate
rispetto all'ambiente underground degli hacklab cui 
spetta per&ograve; il compito di spingere verso questa direzione,
creando un fronte comune d'opinione che abbia una chiara
posizione politica.<br><br>

Un altro passo verso la diffusione del software libero &egrave; una
forte promozione a livello pubblico. Non tutti, infatti,
sono al corrente dell'esistenza di un'alternativa a
Microsoft e per questo gli operatori del software libero
hanno deciso di impegnarsi nella diffusione di questa nuova 
proposta sia attraverso corsi di base sia con lo sviluppo di
interfacce, programmi e manuali sempre pi&ugrave; accessibili all'utente meno
esperto e che vuole dal computer principalmente la facilit&agrave;
d'uso.<br>
I lavori si sono conclusi domenica pomeriggio con
un'assemblea conclusiva in cui si sono chiariti questi
punti d'intervento per raggiungere quell'obiettivo della
diffusione del software libero.<br><br>

Il prossimo appuntamento &egrave; stato stabilito per aprile a Roma
in cui si &egrave; deciso di invitare tutte le comunit&agrave; di operatori del
software libero nonch&eacute; i rappresentanti delle istituzioni pi&ugrave; sensibili alla
questione per cominciare ad intavolare dei progetti concreti e
politicamente chiari di diffusione, anche istituzionale,
del software libero.</div>
