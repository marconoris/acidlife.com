<div align="justify" class="txt">Collegamenti sempre meno costosi, velocit&agrave; di collegamento sempre pi&ugrave; marcate. Un bene. Ma l'abbattimento dei costi per 'usare' Internet e prestazioni in costante miglioramento non sembrano coincidere con un futuro di maggiore democrazia in rete. Lo dimostrano la nuova legge sul copyright (18 agosto 2000, n. 248), la futura convenzione sui reati informatici in discussione alla Comunit&agrave; Europea, il tentativo di
variare la legislazione attuale sui brevetti sul software (ed il suo fallimento, almeno alla Conferenza di Monaco), le battaglie contro Napster e la determinazione ad imbrigliare movimenti di pensiero che si manifestano pi&ugrave; facilmente sotto forma di file sharing che di vertigizzazione del sapere. Situazioni che, se hanno un effetto diretto e prevedibile sul mondo del software e ne mettono in discussione la libert&agrave; di sviluppo, altrettanto si ripercuotono su settori come la musica, l'arte e la letteratura, pienamente presenti in rete e forme d'espressione in costante espansione.<br><br>Questo meeting vuole essere un momento per fare il punto della situazione, per capire quanto si sta cercando di mettere regole alla rete usando strumenti come il copyright. E come, invece, gli stessi strumenti possano venire utilizzati come alternativa ad un sistema di controllo.<br><br><div class="big">Il programma</div><br>
